<p> 
I used to offer a prize for any journalist in my team who could translate the term “capacity-building” into plain language for a general audience. I’m still waiting...
</p>
<p>
Jargon and specialist terminology are rife in the humanitarian and development sectors - and DRR has its own unique vocabulary. 
</p>
<p>
I’d never advise journalists to use jargon in their reporting. But it is important to know what DRR specialists are talking about.
</p>
<p>
First, to understand the concepts and avoid presenting the wrong picture.
</p>
<p>
Second, to explain things clearly to your audience.
</p>
<p>
And third, so you can seek out stories where others might not see them, simply because they’re not DRR-literate.
</p>
<p>
A golden rule when it comes to terminology: never be afraid to ask experts to explain what they mean in everyday language. Your job is to simplify the language of experts for your audience - while not distorting the meaning.
</p>
<p>
Another tip: Specialist language can make things feel abstract: “community-based disaster risk management”, “non-structural measures”, “underlying disaster risk drivers”... I find it hard to picture what these phrases mean.
</p>
<p>
Always be grounded. A good way to bring things back to earth is to make things concrete. So, if you must say “hydrometeorological hazards”, at least give us a couple of examples. Say: “hydrometeorological hazards like cyclones and storm surges”.
</p>
<p>
To help, we’ve created a glossary of key terms. You’ll find it in this module. Please take time to familiarise yourself with it. And feel free to download it for future use.
</p>	  
	  
