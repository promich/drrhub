		<p class="lead mb-4">
The term “disaster” refers to widespread disruption caused to people’s lives and property by an event or a series of events. If a natural hazard causes no disruption, it does not result in a disaster. Thus, though many hazards may be inevitable, disasters are not. We cannot prevent a volcanic eruption, but we can prevent it from becoming a disaster.</p>
<p class="lead mb-4">
The first step you could take as a reporter is to avoid the term “natural disaster” and use instead “disasters” or “natural hazards”. This will help change the way that opinion leaders and the public at large perceive disasters. By doing so, you will help create a culture of prevention and not just a culture of reaction.
