<p> 
And finally in this block we come to the difficult subject of "Ethics". We can't cover every aspect of a story so Media coverage is often selective, it creates narratives that gather momentum and that determines how disasters and the people involved are viewed by the public. Our reports  often determine where blame is laid, and who is blamed for failures or appreciated for an effective disaster response. This has been seen all over the world in the various national responses to the COVID-19 pandemic.
</p><p>
We're sometimes guilty of "hunting in packs" - journalists can be guided by the angles covered by other journalists. IN OTHER WORDS, once a topic gains attention, it attracts more attention â€“ and because it attracts more attention it creates a news wave. We have to recognise this and try to break this loop where we can.
</p><p>
Dealing with Survivors around a disaster is always a difficult area. Sometimes we're first on the scenes. Our primary job is to get the story out to the rest of the world but there may be injured or distressed people around us needing treatment or food. This is a real dilemma that many disaster journalists face.
</p><p>
So we've come up with another checklist that we hope you'll find useful... for example - Avoid sensationalising any disaster situation,' be sensitive to survivors and their needs, don't get in the way of rescuers; think about the impact of showing distressing visuals, deal only in facts and don't blame people ... take a look at the checklist to make sure you're on the right track. </p>
<p>And one more thing - Information gathered from social media is invaluable  but it also presents a number of problems. Content circulating on social media can often include rumours, fear-mongering, one-sided perspectives, misguided solutions, unfounded predictions, false cures, so be very wary - check and double check your sources.
</p><p>
These are nearly always complex stories - so stick to the verified facts and use the experts in the right field.
</p><p>
