	  <p>

In this module, broadcasting members will get acquainted with the role of the 193 WMO members and Territories as a United Nations specialized agency on weather climate and water.
</p><p>
The WORLD METEOROLOGICAL ORGANIZATION is recognised by the UN as the authoritative source for weather and climate information.  Within the United Nations, WMO is the specialized agency responsible for coordinating climate and weather research;developing standards and advancing technology; coordinating its Member States for the observation, analysis, data exchange, and forecasting of weather, climate, water and environmental conditions.
</p><p>

THE WORLD METEOROLOGICAL ORGANIZATION represents a global network of national meteorological & hydrological services providing weather and water agencies and governments to reduce risk from disasters related to weather and climate. 
</p><p>

The World Meteorological Organization is a signatory to the The Sendai framework for disaster risk reduction - but so too are its members the National Meteorological and Hydrological Services - coordinating efforts to lessen human and property losses through improving forecast services and early warnings, assessing risk, and raising public awareness.
</p><p>

The WMO supports climate policy-making by providing authoritative advice and information on climate change mitigation and adaptation. WMO draws from the best available scientific expertise from the National Hydrological and Meteorological Services of its Members, and international data centres and agencies. Moreover, WMO promotes a better understanding of the societal impacts of climate change within the United Nations system. All weather and climate information including forecast and warnings originate from the WMO  network.
</p><p>
Our dependence on weather is increasing. Thereâ€™s more demand for and dependence
on weather information. The ability to confidently mobilize regional and international responses to disasters hinges on the reliability and trust engendered by the routine observations provided 24 hours per day, 365 days per year by the World
Meteorological Organization.
</p><p>
OBSERVING THE EARTH FOR HAZARDS
</p><p>

The WMO exchanges billions of observations in real-time across its network called the WMO Global Operational Network. 
This stream of weather and climate data flowing  in and out of  national Meteorological and hydrological services also includes the latest  environmental data that supports Multi-Hazard Early Warning Systems.
</p><p>
The WMO Global Operational Network is a web of individual systems.
The WMO Global Observing System makes meteorological and climatological observations and related environmental observations at stations
on land and at sea, and from aircraft, meteorological environmental satellites and other platforms;
</p><p>
OBSERVING THE LAND
</p><p>
	All land-based weather stations whether they are manual or automatic have standard equipment to record rainfall, temperature, pressure, wind,
sunshine and humidity. Radar has become the most indispensable piece of warning
equipment used at national weather services. Radar is a tool for nowcasting, that is observing and forecasting weather conditions up to  two hours ahead. Forecasters can warn the public of imminent threats such as tornadoes and severe thunderstorms.
</p><p>
Radar is also useful to observe heavy rainfall from tropical cyclones once within range nearer land. 
After landfall a Tropical cyclone can travel hundreds of kilometres away from the coast. Its heavy rains cause significant flooding requiring issuing of alerts, watches and  warnings of flash flood and or river flood to communities now well inland away from the point of landfall. Disaster managers and responders can make timely decisions based on accurate real-time information on hazards.
</p><p>
OBSERVING THE SEA
</p><p>There is more data on our seas and oceans than ever before. marine meteorological and climate data from various observing platforms has risen dramatically. Observations collected have risen from 1.5 million to more than 9 million observations - from the traditional weather readings on ships to relying increasingly on sophisticated drifting and moored buoys.
</p><p>
OBSERVING IN SPACE
</p><p>The space-based observing system, initially begun for use by operational meteorologists related to atmosphere, ocean and terrestrial surfaces, now has a particular emphasis on climate monitoring and disaster risk reduction.
</p><p>
Managing and exchanging data and information about weather, climate and water belongs to the WMO information system. By August 2020, WWIS had provided official weather information for 2924 cities, of those 2791 cities get forecasts from the 139 national met services, meanwhile 2137 cities are available with climatological information from 170 members. 
</p><p>
Global Telecommunication System is the network that ensures the rapid, reliable collection and distribution of observations and processed information across the globe.
</p><p>
WMO Global Data-processing and Forecasting System consists of three World Meteorological Centres and 40 Regional Specialized Meteorological Centres. These centres run and supported by National Meteorological Services function as archives for past weather data, and processing and forecasting hubs at the global and regional levels. This coordinated system provides analysis, modelling, forecasting and other products in support of forecasting and early warnings of weather and climate hazards.

