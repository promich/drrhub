<p> 
"Hello and welcome to this World Broadcast Project dealing with Disaster Risk Reduction.
</p>
<p>
Essentially, this is an online course that would subsequently see a physical and practical follow-up with you the participants in your own regions.  We all realise however that every aspect of human life around the world has been affected by the Coronavirus pandemic. Our follow-ups therefore will be in the form of interactive, digital meetings.
</p>
<p>
What COVID-19 has highlighted though, is the urgency and importance of concerted planning and collective action to reduce disaster risk and build resilience.
There are 6 Blocks within this course ranging from the technical requirements of early warning systems, to practical guidelines for broadcasters to reporting and programme ideas for content providers.
</p>
<p>
The Quiz elements at the end of the sections will then earn successful participants an internationally recognised certificate.
</p>
<p>
But before you get started with the information and concepts involved, sit back and listen to these key messages from some of the international organisations and broadcast unions behind this project.
</p>
<p>
Broadcasters across the world are attempting to establish a long-term capacity building programme to increase their reporting on climate adaptation and Disaster Risk Reduction but fulfilling this aim, as part of the Sendai Frameworks's plan of action to reduce disaster risks by 2030, calls for a unique global partnership....."
