<p> 
By now you should have a pretty good idea of the basics of DRR.
</p>
<p>
Next, we’re going to look at how to apply what you’ve learned to news and current affairs.
</p>
<p>
And in fact, soon you’ll be discussing this in person with a trainer via video conference.
</p>
<p>
But just to show you how this is a story that cuts across beats, I’d like to give you 10 good reasons to report on DRR.
</p>
<p>
One. DRR is a political issue.
</p>
<p>
As disasters continue to rise and people demand more action from their governments to take preventative action, DRR will become a hot potato politically.
</p>
<p>
The effects of climate change have already led to civil unrest and even armed insurgencies.
</p>
<p>
The increasing damage from disasters will also make the case for stronger governance and closer regional and international collaboration.
</p>
<p>

Two. Natural hazards are on the rise.
</p>
<p>
Climate change, poverty, urban risks and environmental degradation are exposing more people to an entirely new scale of devastation.
</p>
<p>

Three. DRR is an economic issue.
</p>
<p>
Disasters cause damage worth $130 billion every year, on average.
</p>
<p>
But it’s not just economic losses.
Disasters devastate livelihoods - sometimes for years.
</p>
<p>

Four. DRR is a human rights issue.
</p>
<p>
It’s the weakest and poorest who suffer most in disasters, raising human rights challenges.
</p>
<p>
And disasters can lead to the scapegoating and demonisation of groups, sometimes fuelled by political agendas.
</p>
<p>
Five. DRR is an environmental issue.
</p>
<p>
Most disasters offer fodder for environmental stories related to cause and effect, or the preventive steps that could be taken.
</p>
<p>
Six. DRR is a cultural issue.
</p>
<p>
People have different perceptions of hazards and react in different ways.
Some see them as actos of God.
Some draw on traditional knowledge for resilience.
This is an area ripe for storytelling.
</p>
<p>
Seven. DRR is a gender issue.
</p>
<p>
There are many reasons why women are more vulnerable to disasters.
</p>
<p>
They may have a more subordinate position in the family.
They may lack education, income, mobility and nutrition, relative to men.
Stories of women’s empowerment are always interesting.
</p>
<p>
Eight. DRR is not only a disaster story.
</p>
<p>
DRR stories don’t just have to be about the disasters themselves.
Covering current risks and dangers,
commemorating past disasters,
reporting on disaster recovery and reconstruction efforts,
looking at positive stories…
All good fodder.
</p>
<p>
Nine. DRR is a health issue.
</p>
<p>
As Covid-19 has shown, the resilience of health systems is essential to strengthen communities’ resilience against natural and also biological hazards.
</p>
<p>
Ten. DRR is often an investigative story.
</p>
<p>
Journalists do more than just break the news. DRR is an area ripe for digging, probing, scrutinising - and holding power to account.
</p>
<p>
That’s just 10 reasons. How many more can you think of?</p>
 
