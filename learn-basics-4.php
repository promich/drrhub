<div class="tab-pane fade" id="transcript2" role="tabpanel" aria-labelledby="transcript2-tab">
<p>
The Sendai Framework for Disaster Risk Reduction is not the first global agreement to tackle DRR. It actually succeeds the Hyogo Framework for Action, which spanned the years 2005 to 2015.
</p><p>
There are some big differences between the two frameworks.
</p><p>
In the further reading below, you’ll see an article by my former colleague, Thomson Reuters Foundation journalist Megan Rowling: “Ten things to know about the Sendai disaster risk reduction deal”. I recommend it. Here are just some of the issues she highlights in her piece.
</p><p>
The first thing to note are the seven targets to drive progress. Hyogo didn’t have those. These targets proved controversial because some countries wanted them to include specific percentage goals. In the end there were none, apart from timeframes for reducing deaths and numbers of people affected by disasters.
</p><p>
Then there’s the issue of finance. Many experts were disappointed that the final Sendai deal didn’t commit clearly to increasing financial aid to help developing countries with DRR. Instead, it settled on a vague target to “substantially enhance international cooperation to developing countries through adequate and sustainable support”. Some felt it was a little woolly.
</p><p>
Now, climate politics burst onto the scene in Sendai – even threatening the prospects of reaching a deal towards the end. That’s not surprising when almost 90 percent of disasters are caused by weather and climate extremes, according to UNDRR.
</p><p>
Some developing countries had wanted the deal to address principles of fairness around who is responsible for causing – and thus providing compensation for – global warming.
</p><p>
The framework does acknowledge climate change as “one of the drivers of risk”. That was language that some governments had tried to remove .
</p><p>
Importantly, Sendai makes a link between the level of support provided to developing states, their differing capacities and the extent to which they will be able to implement the agreement.
</p><p>
The next thing to note is the connections the framework has with other 2015 deals. These are notable. But some development experts say there could have been better linkages with the UN climate talks and the Sustainable Development Goals.
</p><p>
It’s worth observing that the Sendai Framework references human-made and technological hazards, which were only a footnote in the Hyogo framework. That’s not surprising since it came in the wake of the 2011 Fukushima nuclear crisis in Japan.
</p><p>
Next, health. There was widespread praise for the much higher profile given to health  matters in the Sendai framework. The same is true for human rights.
</p><p>
The final text mentions “promoting and protecting all human rights, including the right to development. This language was welcomed by rights experts. Displacement is also a feature of the framework. It notes that 144 million people were forced from their homes between 2008 and 2012 by disasters.
It urges effective policies to address this.
</p><p>
Importantly, the Sendai Framework also includes a statement that DRR policy and practice requires a gender perspective. It says women should play a leading role in disaster prevention and response.
</p><p>
And finally, the framework recognises the need to keep livestock and working animals safe in disasters. More than 1 billion of the world’s poorest people rely on animals for food, transport and their livelihoods.
</p><p>
There’s much more in Megan’s article. Check it out below.
</p>

