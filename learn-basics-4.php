<p> 
In the next couple of videos, we’ll look at global agreements that are crucial to shaping the DRR agenda. The most important is the Sendai Framework for Disaster Risk Reduction.
</p><p>
The Sendai Framework is one of the pillars of the 2030 Development agenda. These include The Paris Agreement on Climate Change, The Addis Ababa Action Agenda on Financing for Development, the New Urban Agenda and, ultimately, the Sustainable Development Goals. We’ll talk about those in a separate video.
</p><p>
So, in March 2015, UN member states agreed a 15-year plan to reduce disaster risk by 2030.
</p><p>
Now, the Sendai Framework recognises that the state has the primary role to reduce disaster risk. But it also acknowledges that others have responsibility: local government, civil society, the private sector and so on.
</p><p>
The Sendai Framework has seven targets.
</p><p>
It aims to reduce four things: Global disaster mortality - in other words, deaths. The number of people affected by disasters. Direct economic losses in relation to GDP. And damage to critical infrastructure and basic services.
</p><p>
It also aims to increase three things: The number of countries with national and local DRR strategies. International cooperation to help developing countries implement the framework. And the availability of multi-hazard early warning systems and disaster risk information and assessment. 
</p><p>
Now, UNDRR is the organisation tasked with supporting the implementation, follow-up and review of the Sendai Framework.
</p><p>
But journalists can also play a role. They can help promote disaster preparedness among audiences. They can ask questions about risk governance and what DRR plans are in place. They can offer a public information service and promote accountability.
</p><p>
In fact, accountability is a cornerstone of the Sendai Framework.
</p><p>
In the further reading below, you’ll find a link to the Sendai Framework Monitor, which is a useful tool to keep track of 38 indicators to track progress across the framework’s seven targets. It’s a resource worth bookmarking for future use.
	
</p>
 
