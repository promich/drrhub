	  <p>Earthquakes. Tsunami. Floods. Chemical explosions… Pandemics... 
	  </p><p> 
A roll call of hazards: natural, technological, biological...
</p><p> 
When calamity hits and the camera crews pile in, there’s a sense of inevitability about disasters. We live in a world where life and livelihoods are fragile. Disasters can upend us at any time - especially if we’re poor, crammed in megacities or living on the frontlines of climate change. 
</p><p> 
It doesn’t have to be this way. Disaster risk reduction is about creating a culture of prevention, not just a culture of reaction. It’s about turning vulnerability into resilience.
</p><p> 	  
What if I told you there’s a story that affects hundreds of millions of people worldwide but has been woefully under-reported? That it cuts across almost every beat, from politics and society, economics and finance, to science and health? And that with a little specialist knowledge, you could own this story in a way few of your peers could imagine?
</p><p> 
That story is disaster risk reduction, or DRR.
</p><p> 
Don’t be put off by the dry-sounding language. DRR brims with journalistic potential, whether you’re doing public service broadcasting, investigative reporting, solutions journalism or producing news and features.
</p><p> 
This course will help you get under the skin of DRR. It’ll demystify the concepts and bust some myths. It’ll help you penetrate the jargon and provide essential context to enrich your reporting.
</p><p> 
There’re plenty of good reasons to get to grips with DRR. Before, during and after a crisis, journalists play a crucial role in disseminating information.
</p><p> 
Getting it right can mean the difference between mitigation and mayhem, recovery and ruin, life and death.
</p><p> 
But it doesn’t stop there. You have the potential to connect the dots between the multi-hazard and systemic risks facing our interconnected world.
</p><p> 
You can raise awareness of the cost of failing to build resilience and the benefits of sound policy and practice.
</p><p> 
You can hold authorities to account for not delivering on their promises and obligations, as laid out in international agreements.
</p><p> 
You can help communities be better prepared to withstand the impacts of all kinds of hazards.
</p><p> 
To do that, you need the basic knowledge in this course by UNDRR. 
</p><p> 
It’s no exaggeration to say DRR is one of the big stories of our age. The stakes are getting higher every day as risk is systemic and as disasters are more and more interconnected. By the end of this module, you’ll be better equipped to do that story justice.	  
</p> 	  
	  
	  
