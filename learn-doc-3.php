	  <p>Let's turn our attention to Documentaries. There's a different Pace to documentaries - you can't hit the audience with facts and data for a sustained period of time... there's more of a wave effect, giving the audience time to absorb an argument or point of view. So planning the elements is vital.</p><p>
Essentially, documentaries tell us something important, something interesting about our world - and in the best examples, they make us think about the world in a new way.  They cover a huge range of topics -  historical events, science, current affairs, the arts, social and political issues, nature,  wildlife and many other areas. But any successful documentary whatever its topic or style has to have one key element - Authenticity! The information must be presented as 'real', and must convince the audience that what they are seeing is genuine.  Again, disasters are real and the attempts to reduce the risks associated with them are real - you don't have to convince an audience about their reality and you therefore have a cast-iron premise to work on.</p><p>
Of course, different documentaries have different purposes – to entertain, inform, educate, ridicule, shock and some are used for propaganda purposes.</p><p>
But the basis for DRR documentaries is usually testimony from interviewees and  actuality footage.  This could be a recording of an actual event which is documented with real people involved - not actors.  Archive footage is used to show a variety of scenarios. It helps the audience to learn about historical knowledge and shows past events which are important to the subject matter. Archive footage is usually used to  set the scene, to give context.</p><p>
If we were studying the whole range of documentary styles, (generally it's considered that there are 6 styles) - we'd be here for a very long time. I suggest that we concentrate on 2 styles - the Expository and the Observational.</p> 	  
<p>
<strong>The Expository</strong> is the classic form of documentary that exposes a story that most people are not aware of in order to engage the viewer. They are often biographical, natural world, historical or talk about a certain event or topic. The main elements  -  A commentator talking over the images or videos shown to explain the story, speaking directly to the audience: a ‘voice of authority’  - seen and heard and usually an expert in the relevant field; You'd also use  graphics & animations  to illustrate  the voiceover.  You'd include factual information and figures; a variety of footage, interviews, stills, archive material to support the argument.</p>
<p>Legal and ethical considerations are particularly important in this mode as the documentary has to be accurate  as you can't provide the incorrect facts on a voiceover and it is important to not unfairly represent someone or a group of people. It's a story built over time and therefore good research and  good planning are essential.
</p><p>
<strong>The Observational</strong> or "Fly on the wall" documentary  allows viewers to observe the subject’s life as if they were currently witnessing it and the subject not being aware of the cameras being there. The ‘Fly-on-the-wall’ basically means to observe but not to interfere. These progrmmes are popular with viewers because they're interested to see what other people are doing with their lives and they allow viewers to reach their own conclusions on the documentary. So for example you might want to follow a week in the life of a farmer who's crops were destroyed and a family member was lost in a storm. The story would give the history through his experiences and show what's been done to prevent such a tragedy from happening again. But you need to be aware of the negatives ! - They're not a true representation of events, only a constructed version for three reasons: Firstly, people act differently around cameras because they're on their best behaviour. Secondly, hours of footage is edited to provide a small representation of the documentary. Therefore because most of the footage is not shown, it doesn’t give a 100% true reflection of events. And lastly, consent and privacy regarding who and what is allowed to be recorded, is a major factor.....a personal family argument may be edited out to protect their privacy.
</p><p>
So ...What are the main elements of observational documentaries?  They're filmed on location;  usually a high use of hand held cameras;  Long takes are necessary to capture as much of the subject’s life as possible;  Synchronous (direct) sound recording;  Very little voiceover is required if at all and interviews only record the voices of the subjects in front of camera; The  Crew are neutral observers – their presence is hidden.  They're ‘on the outside looking in’;  There are no rehearsals or staged events - the crew have to follow the subject and be able to move without notice. This can result in shaky looking footage and imperfect sound but it's authentic.</p>
