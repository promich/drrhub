<p>
Our next topic concerns "Laws, Regulations, Policies, and Plans".
</p><p>
National legislation, rules, and regulations are very important to emergency management. These can be the basis on which the country: defines the responsibilities of those who play a role in emergency management, determines coordination mechanisms, identifies decision-makers at relevant agencies. Legislation and rules contribute to the sustainability of disaster risk management when they outlast any particular government administration.
</p><p>
Broadcasters must be aware  of national disaster risk management plans. These provide interagency and multi-sector frameworks for actions of government and civil society in the face of a potentially disastrous hazard.
</p><p>
National plans should be based on the premise that disaster risk management is the responsibility of public, private and civil society participating in a multi-sector and interdisciplinary manner. There should be a clear understanding of roles and authorities across all stakeholders. Ideally, planning includes the highest levels of national government, with appropriate organizational and leadership support. Of course, it is essential that resources are provided as necessary to deliver and maintain the desired outcomes.
</p><p>
The major emergency management actors in the country should have developed Standard Operating Procedures (SOPs). These are detailed instructions on how to carry out the specific operational tasks or activities of emergency management. Of course, in actual practice we anticipate that not all actors will adhere completely to published plans and procedures every time.
</p><p>
Here we call out a National-level planning document that is especially relevant to Early Warning Systems: the National Emergency Telecommunications Plan (NETP).
</p><p>
According to the International Telecommunication Union (ITU), an NETP sets out a strategy to enable and ensure communications availability during the disaster mitigation, preparedness, response and recovery phases, by promoting coordination across all levels of government, between public and private organizations, and within communities at risk.
</p><p>
The effective management of the risk of disasters depends on communication and information sharing. In particular, timely and effective information flow is important for early warning that helps minimize loss of lives and livelihoods.
Recommendation 6 in the ITU Guidelines for national emergency telecommunication plans is quite specific:
</p><p>
"Multi-hazard early warning systems should be designed and deployed, linking all hazard-monitoring systems when possible to take advantage of economies of scale and enhance sustainability and efficiency through a multipurpose user-centric framework. An inventory of such systems, together with the processes used to activate them, should be included in the NETP and periodically reviewed and updated."
</p><p>
Given this context, now let's consider some characteristics of "Early Warning Systems".
</p><p>
Target G of the Sendai Framework for Disaster Risk Reduction calls for nations to substantially increase the availability of and access to multi-hazard early warning systems and disaster risk information and assessments to people by 2030.
</p><p>
Six Indicators are used to measure global progress on implementation of Target G in the Sendai Framework.
</p>
<ul>
<li>
Number of countries that have multi-hazard early warning systems.
</li><li>Number of countries that have multi-hazard monitoring and forecasting systems.
</li><li>Number of people per 100,000 that are covered by early warning information through local governments or through national dissemination mechanisms.
</li><li>Percentage of local governments having a plan to act on early warnings.
</li><li>Number of countries that have accessible, understandable, usable and relevant disaster risk information and assessment available to the people at the national and local levels.
</li><li>Percentage of population exposed to or at risk from disasters protected through pre-emptive evacuation following early warning.
</li>
</ul>
<p>
Early warning systems seek to provide timely and actionable information, to the public as well as to others involved in the emergency.
</p><p>
Early warning systems can include: hazard threat monitoring, forecasting and prediction, risk assessment, communication, and other activities, systems and processes that enable individuals, communities, governments, businesses and others to take timely actions that protect lives and livelihoods, before and during emergencies.
</p><p>
Here we look at four elements of Early Warning systems.
</p><p>
Disaster risk knowledge
</p>
<ul>
<li>Are key hazards and related threats identified?
</li><li>Are roles and responsibilities of stakeholders identified?
</li><li>Is risk information consolidated?
</li>
</ul>
<p>
Detection, monitoring, analysis and forecasting of hazards and consequences.
</p>
<ul>
<li>Are monitoring systems in place?
</li><li>Are forecasting and warning services in place?
</li><li>Are institutional mechanisms in place?
</li>
</ul>
<p>
Dissemination, by official sources, of authoritative, timely, accurate, and actionable warnings.
</p>
<ul>
<li>Are organizational processes in place and operational?
</li><li>Are communications systems in place and operational?
</li><li>Are early warnings communicated effectively to prompt action?
</li>
</ul>
<p>
Preparedness at all levels to respond to the warnings received.
</p>
<ul>
<li>Are disaster preparedness measures, including response plans, developed and operational?
</li><li>Have public awareness and education campaigns been conducted?
</li><li>Have public awareness and actual responses been tested and evaluated?
</li>
</ul>
<p>
The fundamental objective of Early Warning is that people in harm's way get messages prompting them to take actions necessary to save lives and livelihoods. However, research shows: people often find emergency alert messages confusing. Even an understood message might be phrased such that it does not prompt actions.
</p><p>
The International Federation of Red Cross and Red Crescent Societies (IFRC) has been working for many years on exactly this problem. The IFRC Public Awareness and Public Education Key Messages are available for use by everyone trying to convey emergency alerts to the public.
</p><p>
These messages are field-verified. Real people in real emergencies give evidence that the messages were actually understood, and the people alerted really did take the actions prompted.
</p><p>
Among scientific and technical agencies, such as a National Weather Service or a Flood Forecasting Service, a set of best practices called "Impact-Based Forecasting" has a similar goal. Instead of merely saying "what the weather will be", the idea is to say "what the weather will do".
</p><p>
Of course, speaking to the audience in terms they understand is basic to journalism. Now the other actors in Early Warning Systems are embracing that principle as well.
</p><p>
Early warning systems can use many communication channels. Television and radio broadcasting is particularly useful when physical access to an area is difficult. During disaster response, broadcasting services can provide information on how and where to access the help that is available, as well as other important information. Appropriate information and advice provided through broadcasted information can help people cope with the disaster until help arrives on-site.
</p><p>
The Internet, social networks, and apps can communicate certain kinds of information and official alerts, helping keep people informed.
</p><p>
Warning systems based on sirens or public address systems connected to sensors can be useful in certain settings to quickly trigger an alert when a set threshold is reached.
</p><p>
Private telecommunication services are those provided through private networks and managed directly by its users. Examples of such networks include firefighters, police, ambulances, relief teams, civil protection, transport, and utilities, as well as other public sector and private sector entities.
</p><p>
Satellite networks provide communications services that have very little dependence on terrestrial infrastructure.
</p><p>
Since 2005, the international standard Common Alerting Protocol (CAP) has been increasingly adopted worldwide.
</p><p>
This simple but general format enables all-hazards alerting over all kinds of media, greatly increasing the efficiency and effectiveness of alerting.
</p><p>
The net result is that people are better enabled to take the actions that can save lives and livelihoods.
</p><p>
The CAP message communicates a few Key Facts of any emergency:
</p><ul>
<li>What is it?
</li><li>Where is it?
</li><li>How soon is it?
</li><li>How bad will it be?
</li><li>How sure are the experts?
</li><li>What should people do?
</li>
</ul>
<p>
As noted already, Early Warning Systems can communicate over many channels. Here are examples of how CAP messages are conveyed to people in harm's way:
</p>
<ul>
<li>Broadcast television can present the CAP alert via crawl text
</li><li>Broadcast radio can present the CAP alert as an audio interrupt
</li><li>Mobile phones can get the CAP alerts through Cell Broadcast or SMS
</li><li>Drivers can see the CAP alert on digital billboards along the highway.
</li><li>Online users can get the CAP alert automatically, through Google online services such as Google Search, Google Maps, or Android.
</li><li>Sirens can be triggered automatically by a CAP alert
</li><li>Smartphones get CAP alerts through a wide range of very useful apps
</li>
</ul>
