<p> 
Let’s review some key DRR messages.
</p>
<p>
If you remember nothing else from this training, these are the points that you want to take away.
</p>
<p>
One. Hazards can be natural or human-made, but they need not inevitably lead to disaster.
</p>
<p>
Two. Disaster risk reduction pays and has an immediate return. It’s not an added cost. It’s an investment.
Disaster risk reduction is often seen as a long-term solution, whereas it is really a short-term solution with immediate returns that will considerably reduce poverty, climate change impacts and disaster risks.
</p>
<p>
Three. Disasters don’t only cause immediate human suffering and destruction. They also impede long-term development by keeping people trapped in a vicious circle of poverty.
</p>
<p>
Four. Disaster risk reduction is about saving lives and livelihoods by changing people’s mindsets.
It is about shifting from disaster response to the management of disaster risks in order to reduce vulnerability and exposure.
</p>
<p>
Five. People have a right to live in safety and with dignity.
It is a state’s responsibility to protect its citizens. It’s therefore vital that DRR policies are systematically integrated into sustainable development strategies at all levels, national to local.
</p>
<p>
Six. Early-warning systems can save lives.
If alarms are sounded before disaster strikes, human loss can be avoided or reduced.
</p>
<p>
Seven. Education is necessary to build a culture of prevention.
People need to be provided with the knowledge, skills and resources to protect themselves from disaster risk, as with health or traffic.
</p>
<p>
Eight. Climate change adaptation starts with disaster risk reduction.</p>
 
