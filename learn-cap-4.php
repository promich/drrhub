	  <p>
Now that you know what a CAP message looks like, I'd like to contrast CAP with prior methods of public alerting.
	  </p><p>
When a major hazard threatens, technical agencies send out notices and public alerting systems kick in. But, each public alerting system has its own particular methods.
	  </p><p>
From local communities to entire nations, societies everywhere have a patchwork of systems, often designed just for particular emergency situations and for particular  communications media.
	  </p><p>
Obviously, this patchwork approach is wasteful. It may also be dangerous if:
	  </p>
<ul>
	<li>
People miss out on alerts they should have gotten.
	</li><li>
People get alerts that are not intended for them.
	</li><li>
People get confusing messages that are difficult to confirm.
	</li>
</ul>
<p>
Using a mixture of text and data, a CAP message communicates key facts about any type of hazard threat, over any and all media.
	  </p><p>
The CAP message can be targeted to: the general public; designated groups such as civic authorities or responders; or specific individuals.
	  </p><p>
Without the CAP structured message format, it was not possible to achieve all-hazard, all-media public alerting on broad scales.
	  </p><p>
Now that we have the CAP standard format, simple tools can be used to get emergency messages to affected people: wherever they are and whatever they are doing.
	  </p><p>
I have said CAP is a kind of "standard business form", and everyone involved in the emergency should have this one additional form.
	  </p><p>
Why should they? Exactly what benefits come from using CAP?
	  </p><p>
For one thing, CAP makes it quicker and easier to issue alerts.
	  </p><p>
Alerting authorities without CAP issue alerts in a variety of ways. Typically, this includes making phone calls, sending Faxes, sending e-mails, posting to a Web page, and posting to Facebook and Twitter, among others.
	  </p><p>
Those activities consume valuable time, distracting from the mission of composing accurate and actionable alerts.
	  </p><p>
With CAP, a single message disseminates quickly over multiple alerting methods.
	  </p><p>
From the perspective of a broadcast news organizations, CAP is the fastest, and least error-prone, way to get alerts from an official source.
	  </p><p>
In a complex emergency, information of many types and from many different sources has to be assimilated, at scales ranging from local to provincial, national, and global. Alerts are a big part of that.
	  </p><p>
Without CAP, this variety of alerts are difficult to get, use, and share, because they are communicated in so many media and formats.
	  </p><p>
Information gathering  and analysis is much easier with CAP alerts. Of course, that is very important for broadcast news organizations, as well.
	  </p><p>
Here we see CAP alerts displayed on a map interface.
	  </p><p>
This supports the Emergency Management function called "Shared Situational Awareness", or maintaining a "Common Operating Picture".
	  </p><p>
Maps like this can be useful to broadcast news organizations as they help their audience to understand a complex or evolving emergency situation.
	  </p><p>
Trust in an alerting system is eroded when people get alerts not intended for them. This happens often with systems based on plain text.
	  </p><p>
With CAP, the alerting area can be precisely defined with polygons or circles in addition to the textual area description.
	  </p><p>
Broadcast news organizations can show this alerting area map to ensure people in their broadcast area know when they are in an affected place.
	  </p><p>
Many people in harm's way are underserved with current public alerting because they are blind, deaf, cognitively impaired, or they do not understand the language used in the alert.
	  </p><p>
These issues can be addressed by exploiting the data features of CAP, and with automated translation.
	  </p><p>
Today there are free tools available to broadcasters supporting the automatic translation of many key facts in a CAP alert. That is proving very useful for automatic insertion of alerts into audio broadcast streams.
	  </p><p>
Some types of hazard occur so suddenly that seconds can mean the difference between timely, life-saving alerts and alerts that arrive too late. Examples are: earthquakes, tornadoes, tsunami, flash floods, volcanoes, landslides, avalanches, and terrorist strikes, among others.
	  </p><p>
CAP alerts can be posted immediately through an online facility that immediately disseminates by many media, sometimes within seconds. And, of course, broadcast news organizations are very adept at getting the story out as rapidly as possible.
	  </p><p>
Also, CAP messages  are digital. That enables immediate action not only by people but by devices such as sirens, highway signs, train controls, and other automated mechanisms that help save lives.
	  </p>
