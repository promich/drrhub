  <div class="tab-pane fade" id="transcript" role="tabpanel" aria-labelledby="transcript-tab">
	  
	  <p>
It is no longer enough to provide a good weather forecast or warning – people are now demanding information about what to do to safeguard themselves, loved ones and protect their property.
	  </p>
	  <p>
Each year the impacts of severe hydro-meteorological events around the earth give rise to multiple casualties, and significant damage to property and infrastructure, with adverse economic consequences for communities that can persist for many years. All this happens in spite of the fact that many of these severe events have been well forecast, with accurate warning information. Sent out in a timely fashion by the responsible national weather forecaster National Meteorological and Hydrological Service. 
	  </p>
	  <p>

Although there is the reality of what the weather might be, there is frequently a lack of understanding of what the weather might do.
	  </p>
	  <p>
 We can point primarily to a gap in the communication between weather forecasts and warnings and understanding its potential impacts,both by the emergency managers  and by the population. 
	  </p>
	  <p>
National weather services and their partner agencies face a challenge to improve the understanding of the potential impacts of severe hydro-meteorological events. 
	  </p>
	  <p>
Moving forward from weather forecasts and warnings to multi-hazard impact-based forecast and warning services is a leap forward in how many national weather services deliver forecasts.
	  </p>
	  <p>

The solution involves conveying the impact of a hazard, or multiple hazards, may have on an individual or community at risk. for example, forecasting the possible impact of rainfall on road users during rush hour, or the impact on passengers of closing railway lines.
	  </p>
	  <p>

Paradigm 1 (...1st gear) Weather forecasts and warnings (hazard only): These types of forecasts and warnings contain information that refers only to atmospheric variables and how they are expected to change. In the case of weather warnings, the focus is on forecasting weather-based hazards only.
	  </p>
	  <p>

Example: “Severe thunderstorms are expected today with wind gusts exceeding 90 km/h.”
	  </p>
	  <p>

Paradigm 2 (...2nd gear) – Impact-based forecasts and warnings (hazard and vulnerability only): These types of forecasts and warnings are designed to express the expected impacts as a result of the expected weather.
	  </p>
	  <p>
Example 1: “ Bora winds are expected tonight which may result in delays or cancellation to ferry services.”
	  </p>
	  <p>
Example 2: “Severe thunderstorms with gusts over 90 km/h will result in damage to trees and power Lines.”
	  </p>
	  <p>

Paradigm 3 (...3rd gear) – Impact forecasts and warnings (hazard, vulnerability and exposure): 
	  </p>
	  <p>
		  These types of forecasts and warnings are designed to provide detailed information down to the individual, activity or community level. For many national weather services  these types of forecasts will fall with partnering agencies rather than the role of the weather service.
	  </p>
	  <p>
Example 1: “Ferry services for the island of Dominica will most likely be cancelled tonight due to strong winds.”

Example 2: “Extensive traffic delays in Singapore may occur due to the risk of large trees downing power lines and blocking roads as a result of severe thunderstorms.”
	  </p>
	  <p>

Hydrometeorological forecast uncertainty refers to the limits of predictability imposed by the state of the science and inherent randomness of the hydrometeorological system. Hydrometeorological forecast uncertainty combines with other factors exposure and vulnerability to help measure risk.
	  </p>
	  <p>
Risks can connect to each other however and compound their effects causing several or many risks at the same time within the same area. 
	  </p>
	  <p>
Forecasters need an ability to compare risks and to make trade-offs, weighing the relative importance of one risk in comparison with another, which may not necessarily be hydrometeorological in character.
	  </p>
	  <p>
Risks are not always easy to identify, quantify and categorize, and sometimes identification occurs long after people felt the impacts of hazardous weather;
	  </p>
	  <p>
Risks are evaluated differently by everyone. Thus, a risk considered serious in one location may be considered less so in another, or there is flexibility in accepting the risk.
	  </p>

