  <div class="tab-pane fade" id="transcript" role="tabpanel" aria-labelledby="transcript-tab">
	  
	  <p>Welcome to the final information block of this  online project. We've attempted to give you a technical and content overview of how - as broadcasters - you can become involved in DRR output. The obvious concentration has been on the best processes to enable risk reduction messages to reach a wider public and we've offered advice for the immediacy and accuracy  of news reports and programmes, in an attempt to be more proactive and more responsible.
</p><p>
But as we've stated, if news gives you the headline story, DRR issues are perfect for going deeper and giving audiences an insight into events that can have a profound effect on their lives and livelihoods. This leads us to a wider programming perspective, beyond the news agenda.
</p><p>
We looked briefly at producing features and Current Affairs that investigate stories and give more background to events - invaluable additions in the "factual programme jigsaw". But let's look at some of the components that make up other productions, that again can bring the broadcaster and the audience closer together.
</p><p>
This means that we need to look at the bigger picture... Chat shows, magazine programmes, educational programmes for children and adults. The Public Service Broadcaster in Thailand - Thai PBS - is an example of a broadcast organisation that has championed DRR issues consistently during the past decade on a number of platforms. NHK do the same in Japan. But what broadcasters are not doing is taking advantage of footage and broadcast ideas that are already out there. Take the FAO for example - the Food and Agricultural Organisation. They produce good quality broadcast stories that can be adapted, amended and used in a local context. Let me give you one example.
</p><p>
We know that extreme weather events take their greatest toll on the most vulnerable groups of people. So the FAO and the Musahar people in Nepal have been working together to become more resilient to such events. Most are landless agricultural workers who live below the poverty line. Extreme weather patterns are affecting their livelihoods.How often do we see that particular set of circumstances repeated across the globe ? In this kind of instance,  when a  river floods the workers have to stay home and when there is drought there is no work. To offset this, FAO offered some technical livestock training to the Musahar people. To get them started, the villagers were given goats and calves â€“ then they attended trainings. Now they are less vulnerable to extreme weather events and have alternative forms of livelihoods that sustain them as their herds grow and reproduce. Take a look at the video and you'll see how this material could be used in an agricultural round up programme or a series that looks at nature and similar local stories could be compared and contrasted.It needs a change of editorial approach and a little bit of imagination.  
</p> 	  
	  
	  
