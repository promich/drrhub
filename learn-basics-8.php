<p> 
Disasters happen for all sorts of reasons, but there are some things you need to know about that aggravate risk.
</p>
<p>
One is a lack of good governance.
</p>
<p>
Principles of good governance include transparency, accountability, efficiency, responsiveness and inclusivity.
</p>
<p>
When these are missing, coordination and enforcement of DRR suffers. People aren’t motivated to be responsible. Corruption flourishes, undermining urban planning, say. 
</p>
<p>
Earthquakes don’t kill; shoddy construction does.
</p>
<p>
Then there’s climate change. We’ve already established that this is an aggravating factor. But to drive home the point, consider this:
</p>
<p>
The Intergovernmental Panel on Climate Change forecasts that by the year 2100, global average surface warming could increase by up to 6.4 degrees Celsius. Remember, 1.5 degrees is the ideal limit under the Paris Agreement.
</p>
<p>
Sea levels could rise by almost 60 centimetres. Tropical cyclones will become more intense. Extreme heatwaves and heavy rainfall will become more frequent. Sub-tropical areas will become more arid.
</p>
<p>
Another aggravating factor is rapid and unplanned urbanisation.
</p>
<p>
One out of every two people live in a city – and the proportion will keep rising. By 2030, five billion of the planet’s projected population of 8.1 billion will be urban.
</p>
<p>
Today, one in three city-dwellers live in marginal settlements or crowded slums, with poor access to basic services like schools, clean water and sanitation.
</p>
<p>
By 2030, two-thirds of humankind will live in cities and three billion in slums.
</p>
<p>
Meanwhile, eight of the 10 most populous cities are vulnerable to earthquakes. And many cities are close to coastlines, making them vulnerable to floods, storm surges and tsunami.
</p>
<p>
Now, let’s not forget about poverty and inequality.
</p>
<p>
Half of the world’s population is vulnerable to disasters because of their social living conditions. And remember that disasters have long-term consequences for poorer people as they have fewer means to recover.
</p>
<p>
Finally, consider environmental degradation.
</p>
<p>
Too often, communities increase the probability – and severity - of disasters by destroying the forests, coral reefs and wetlands that might have protected them.
</p>
<p>
Forests once covered 46 percent of the Earth’s land surface. Half of these have disappeared.
</p>
<p>
Coral reefs are home to a quarter of all marine species. Sixty percent of reefs could be lost in the next 20-40 years.
</p>
<p>
Desertification threatens a quarter of the planet’s land surface, potentially affecting a billion people.
</p>
<p>
Climate change will only make things worse.</p>
 
