                        <div class="mb-4">
                          <h4 class="h3 mb-3">Responsibility of the media in disaster reporting</h4>
                          <p>Whether we like it or not, the way a media organization responds to a disaster will be driven first and foremost by how strong a story they think it is. To put it crudely: How bad is it? When reports come in of a catastrophic flood or an earthquake, the journalistic instinct will be to ask questions like these:</p>
<ul>                          
<li>How many people are dead, injured, made homeless? (Enough to mean that I have to run the story)</li>
<li>Are people from my own country likely to be among the victims? (If not, am I interested?)</li>
<li>What are the video pictures like? (If they’re good, I might run them, regardless of the answers to the previous two questions)</li>
<li>Should a journalist be sent to the scene, and if so, how far away is it, how long will it take to get there and how much will it cost? (My budget’s always under pressure)</li>
<li>Will my audience be interested in the story? (And will they care?)</li>
<li>What other news is there today? (I never have enough room for all the news I want to carry)</li>
<li>Might I win an award for covering this? (That would be nice)</li>
</ul>
<p>
These are the selfish, but perfectly understandable reflexes of news people the world over, be they employed by commercial or public service organizations. And even when a disaster satisfies these editorial requirements, it cannot be guaranteed to command any volume of coverage. A famine in Africa might fall off the editorial list on the grounds that it has been going on for a long time and is likely to continue for some time. Or, there is nothing new to say, and there is a sense of helplessness that no one can do anything to prevent it. Widespread flooding in Bangladesh, for example, might not figure in an editorial discussion because of the regular, seasonal nature of such occurrences. It’s not news.
</p><p>
This may perhaps sound callous, and it is certainly depressing, but surely it is also inevitable. By some computations, there is, literally, a disaster for every single day of the year. Many will pass virtually unnoticed. Most will receive news coverage in the region in which they occur, but few will feature on a global news agenda. Frustratingly, those that do will not always be the ones most deserving of attention. Think of the huge international coverage given to the floods in Mozambique some years back.
</p><p>
Was that a reflection of the number of dead, the number of homeless, economic or environmental damage? It was surely a lot more to do with all those dramatic pictures of helicopters plucking people to safety, and the story of the woman who gave birth in a tree while awaiting rescue.
</p><p>
Elsewhere in this book you will find powerful arguments intended to change that mindset, helping journalists focus on prevention rather than cure, on early warning and explanation, and on subsequent efforts to rebuild and recover. It is much harder to sell these stories to news organizations that are conditioned simply to reporting events as they occur, and which have a limited attention span and little interest in context or background. But many will see it as part of their responsibility to take a broader and more multifaceted approach to their journalism.
</p><p>
And that broader approach should also drive their actual reporting of an event. If the reportage will be seen or heard in the affected area, there is obviously a huge public information remit for the media. This could take many forms, including information on which areas are worst affected, weather forecasts, where to find shelter, water, food and other necessities. To this might be added news of the hospitals treating victims, and where it is possible to find news of people who might have been caught up in the disaster.
</p><p>
This primary phase of the disaster coverage will often see the media, governments, emergency services and relief organizations working together to get the maximum amount of information to the maximum number of people. All of this is a legitimate, indeed obligatory, part of the media’s reporting effort. To this extent, everyone is a public service broadcaster in these circumstances. Sometimes, audiences will want to know what they can do to help with money, food, clothing, medical supplies and the coverage can advise them on what is most needed and how it can be conveyed to the disaster zone.
</p><p>
Audiences will also want to know more about what has happened and why. Many disasters can be traced to a cause, manmade or natural. People the world over were desperate to know the cause of the tsunami, which had such a devastating impact over such a wide area. Mudslides can sometimes be linked to deforestation many miles upstream. Increasingly, people look for links to global warming and climate change.
</p><p>
Often, too, there is a natural human desire to hold someone accountable, to find someone to blame. Did a government ignore calls for early warning systems, or skimp on defences against hazards? Did it turn a blind eye to excessive logging or toxic emissions from a chemical plant? Did a company ride roughshod over safety regulations because they would have hit profits? Were the forest fires started on purpose?
</p><p>
All of these are important areas for journalistic exploration, not least because they contribute towards efforts that can be made either to prevent such a disaster ever happening again or if that’s not feasible mitigating the effects, should it strike again in future. News organizations should feel the need to keep returning to stories to make sure that promised new regulations have indeed been put into place, that overseas aid has gone to those who most need it, that reconstruction is proceeding at a reasonable pace. Holding people accountable for their areas of responsibility is one of the basic purposes of a properly functioning media.
</p><p>
From all of which it will be evident that even given their blinkered and highly subjective response to a disaster, the media can and should take a prominent role in bringing it to public attention, support the relief effort by the rapid dissemination of information, explain the background and causes, and hold people to account for their actions. These are responsibilities that most news organisations would recognize and readily accept.
</p>

