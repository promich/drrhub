		          <h3 class="mb-4">Poverty and inequality</h3>
            	</div> 
			</div>      
    </div>
<div class="col-md-9 col-lg-5">
	<p class="lead mb-4">
Poverty and socio-economic inequalities are aggravating disaster factors. They not only make poor people more vulnerable to disasters, they also trap them in a vicious circle of poverty. Reducing poverty will reduce disaster mortality, vulnerability and exposure and help achieve the Sustainable Development Goals. 
</p>

<h4 class="mt-4">The facts</h4>
<ul>
<li class="mb-2">Disasters affect poor countries and poor communities disproportionately. The World Bank reports that: This disproportionate effect on developing countries has many explanations. Lack of development itself contributes to disaster impacts, both because the quality of construction often is low and building codes, and registration processes, and other regulatory mechanisms are lacking, as well as numerous other development priorities displace attention from the risks presented by natural events ( Hazards of Nature, Risks to Development, World Bank 2006).

<li class="mb-2">A country’s level of development has a direct impact on the damage natural hazards inflict on populations. Less-developed countries suffer most, as their weak infrastructure and limited capacity for prevention makes them more vulnerable than wealthy, industrialized nations.</li>

<li class="mb-2">One half of the world population is vulnerable to disasters because of their social living conditions. Slums and poor urban settlements are the most exposed to disasters.
The poor are more likely to occupy dangerous, less desirable locations, such as flood plains, riverbanks, steep slopes and reclaimed land.</li> 

<li class="mb-2">Poor people tend to live in poorly built and unprotected buildings that are more vulnerable to disasters.</li>

<li class="mb-2">Such overexposure is also true for drought and high temperatures in most countries.</li>

<li class="mb-2">Disasters have long-term consequences for poorer people as they have less means to recover. When poor people are impacted by disasters, they generally lose a greater proportion of their assets. A recent report from the World Bank reveals that the impact of disasters forces some 26 million people into poverty each year (Source: World Bank and the Global Facility for Disaster Reduction and Recovery (GFDRR) in November 2016).</li>

<li class="mb-2">Poor people receive less post-disaster support than do other sections of society. For example, after the floods and landslides in Nepal in 2011, only 6 percent of the very poor sought government support, compared with almost 90 percent of the well-off (Gentle et al. 2014).</li>

<li class="mb-2">Disasters have long-lasting effects on education and health among the less well- off because they may have to make detrimental choices such as withdrawing a child from school or cutting health care expenses. In Peru, the impact of the 1970 Ancash earthquake on educational attainment could be detected even for those born to mothers who were children in 1970, demonstrating that the effects of large disasters can extend even to the next generation (Caruso and Miller 2015).</li>
</ul>

<div class="bg-white p-4 mb-4 mt-4">
<h4>Poverty and inequality: What can be done? </h4>
<p class="lead">Establish urban development programmes that reduce the creation of slums in risk areas and prevent the growth of housing on dangerous slopes or flood plains.</p>
<p class="lead">Provide the poor with access to lands that are safe.</p>
<p class="lead">Involve the poorest communities in building their own capacity to resist disaster since they have most to lose, and to give them a greater political stake in the community.</p>
<p class="lead">Give the poorest people full access to early warning systems, preparedness measures and at the same time access to financial mechanisms that can help them protect their homes, health and livelihoods.</p>
<p class="lead">Develop micro-finance mechanisms that include micro-credits, micro- savings and micro-insurance -- instruments that help reduce poverty by also reducing vulnerability to natural hazards.</p>
</div>	

