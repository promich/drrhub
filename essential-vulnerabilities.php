		          <h3 class="mb-4">Lack of governance</h3>
            	</div> 
			</div>      
            
    </div>
<div class="col-md-9 col-lg-5">
	<p class="lead mb-4">
Natural hazards on their own do not result in disaster. Governance influences the way in which national and sub-national actors (including governments, parliamentarians, public servants, the media, the private sector, and civil society organizations) are willing and able to coordinate their actions to manage and reduce disaster-related risk. Principles of good governance include broad participation, transparency, accountability, efficiency and responsiveness. All are as important for disaster risk reduction as they are for development at large.  Good governance is essential for effective disaster risk reduction and sustainable development. </p>
<p>
The Sendai Framework for DRR lists strengthening disaster risk governance to manage disaster risk as one of the four priorities for action. A clear first step to promoting stronger governance for disaster risk reduction is improving relevant laws and regulations as well as strengthening their implementation.</p>

<h4 class="mt-4">The facts</h4>
<p>Recent examples have shown that when disaster risk reduction is a priority on the political agenda, populations and assets are better protected and more lives are saved.</p>
<ul>
	<li class="mb-2">
Countries which suffer from weak or poor governance are more likely to expose their populations and communities to disasters due to lack of coordination and enforcement. In Nepal during the last earthquake in 2015, the fact that building codes were not enforced aggravated mortality. The lack of strong institutions and governance bodies in Haiti has been a major problem in the reconstruction process after the 2010 earthquake.
	</li><li class="mb-2">
Poor governance does not motivate people to be accountable and responsible. If people are not well informed and educated about what they should do when, for example, early warning messages are issued, they do not evacuate on time and are more exposed to disaster impacts.
</li><li class="mb-2">
Poor governance also facilitates corruption and makes disaster risk reduction policies less effective; this is in particular true in urban planning policies. The lack of enforced laws especially in the construction sector can put more populations at risk of disasters. Earthquakes don’t kill people, it’s poor quality construction that usually does.
</li><li class="mb-2">
Good governance means also to involve different vulnerable groups in disaster risk management, when women, older persons and persons living with disabilities, participate in disaster risk management policies they are less vulnerable to disasters.
</li><li class="mb-2">
Lessons learned after events such as the 1995 Kobe earthquake, the 2004 Indian Ocean tsunami, the 2008 Sichuan earthquake, Hurricane Katrina in 2005, and the 2010 Haiti earthquake show that governance and disaster risk governance are key to saving lives.  Greater stakeholder involvement, capacity building, decentralisation and devolution or the transfer of power and authority to local levels are important elements to better protect people and critical infrastructure.
</li> 
</ul>


<h4 class="mt-4">Examples of good disaster governance </h4>
<p><strong>Indonesia</strong>: President Susilo Bambang Yudhoyono was the first Head of State to convert the Hyogo Framework for Action 2005-2015: Building the Resilience of Nations and Communities in Disasters into a national plan following which Indonesia adopted a new Disaster Risk Reduction law in 2007. President Yudhoyono also increased the amount of money allocated to disaster risk reduction in his national budget and helped build more resilience. In his keynote address at the 5th Asian Ministerial Conference on Disaster Risk Reduction in 2012, President Yudhoyono explained why his administration has made disaster reduction a top national priority: "Disasters in all its forms — tsunamis, earthquakes, forest fires, floods, landslides, volcanic eruptions — are the greatest threats to our national security and public well-being,” he said.</p>

<p>
<strong>Mexico</strong>: Prior to the arrival of Hurricane Patricia on October 2015, the Government of Mexico led by President Enrique Peña Nieto issued multiple warnings to coastal communities. A hurricane watch was first raised at 09:00 UTC on 21 October, 2015 encompassing areas of Michoacan, Colima, and Jalisco. A tropical storm watch also covered portions of Guerrero. As Patricia intensified, the government issued a hurricane warning for areas between Cabo Corrientes, Jalisco, and Punta San Telmo, Michoacan; a tropical storm warning supplemented this for areas further north and east. The hurricane warning was extended northward to include areas south of San Blas, Nayarit, on 23 October. Once the storm moved inland and the threat of damaging winds diminished, these warnings were gradually discontinued on 24 October. Thanks to this huge mobilization and coordination, people were prepared to act and lives were saved.
</p>
<p>
<strong>China</strong>: China, which annually faces almost every disaster possible - including earthquakes, floods, cyclones and landslides has learned from the experience of many years of heavy human and economic losses and announced in April 2013 that the country will now aim to keep disaster losses at no more than 1.5 percent loss of GDP. On average, disasters had cost 2.38 percent of China's GDP in the previous 20 years said an expert from the National Disaster Reduction Commission (NDRC) in China.  
</p>


<div class="bg-white p-4 mb-4 mt-4">
<h4>Lack of governance: What can be done?</h4>
<p class="lead">Put in place national and local strategies for disaster risk reduction aligned with the priorities for action in the Sendai Framework</p>
<p class="lead">Make disaster risk reduction a priority in national development plans and sectoral legislation.</p>
<p class="lead">Have a national budget on DRR.</p>
<p class="lead">Have DRR laws to implement DRR policies. </p>
<p class="lead">Enforce and implement disaster risk reduction laws.</p>
<p class="lead">Adopt a holistic approach across sectors and levels regarding funding and institutions. </p>
<p class="lead">Decentralize DRR responsibilities. </p>
<p class="lead">Adopt coherence between climate change adaptation, poverty reduction and disaster risk reduction approaches. </p>
