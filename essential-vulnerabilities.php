		          <h3 class="mb-4">Climate change</h3>
            	</div> 
			</div>      
            
    </div>
<div class="col-md-9 col-lg-5 mt-sm-2">
	<p class="lead mb-4">
Climate change will create new hazards such as glacier melting, sea level rise and extreme weather events on a scale never seen before. This will aggravate the existing disaster risks and vulnerabilities and expose millions of people never affected before around the world.</p>

<h4 class="mt-4">The facts</h4>
<p>In its Fourth and Fifth Assessment Reports, the Intergovernmental Panel on Climate Change (IPCC) predicted that by 2100:</p>
<ul>
<li class="mb-2">
Global average surface warming will increase by between 1.1 and 6.4 degrees Centigrade.</li>
<li class="mb-2">
Sea level will rise by between 18cm and 59cm; sea-level rise, coupled with coastal storms, will increase the risks of flooding and threaten protective ecosystems.</li>
<li class="mb-2">Oceans will become more acidic and warmer.</li>
<li class="mb-2">Extreme heatwaves and heavy rainfalls will become more frequent.</li>
<li class="mb-2">More heatwaves will increase death rates among the elderly, very young, chronically ill and socially isolated.</li>
<li class="mb-2">Higher latitudes will experience more precipitation; subtropical land areas will become more arid.</li>

<li class="mb-2">Tropical cyclones (including typhoons and hurricanes) will become more intense, with higher peak wind speeds and heavier precipitation, as tropical sea surface temperatures increase.</li>
<li class="mb-2">Regions hardest hit will include the Arctic, sub-Saharan Africa, small islands, developing states, Asian deltas and coastal zones.</li>
<li class="mb-2">Increased drought in some regions will lead to land degradation, crop damage and reduced yields; livestock deaths and wildfire risks will increase, and people dependent on agriculture will face food and water shortages, malnutrition and increased disease, with many being forced to migrate.</li>
<li class="mb-2">Greater rainfall in some areas will trigger more floods and landslides, with consequent disruption to agriculture, urban settlements, commerce and transport.</li>
<li class="mb-2">Increases in the number and intensity of powerful cyclones will affect coastal regions and threaten very large additional losses of life and property.</li>
<li class="mb-2">As temperatures rise, glaciers melt, increasing the risk of glacial lake outburst floods; as mountain glaciers recede, farmers and towns downstream that depend in the summer months on glacial melt water will increasingly be at risk.</li>
</ul>

<div class="bg-white p-4 mb-4 mt-4">
<h4>Climate change: What can be done?</h4>
<p class="lead">Make disaster risk reduction a national and local priority, with strong institutions to implement decisions.</p>
<p class="lead">Set up early warning systems that reach all people, in time for appropriate action, and accompany the warnings with helpful advice.</p>
<p class="lead">Incorporate climate risk in all urban planning and water and forest management processes.</p>
<p class="lead">Maintain and strengthen coastal wave barriers, river levees, flood ways and flood ponds.</p>
<p class="lead">Have adequate drainage systems to avoid flooding.</p>
<p class="lead">Incorporate climate risks in infrastructure projects, especially in hospitals, schools and water supplies.</p>
<p class="lead">Support diversification, including new sources of income, new crops and agricultural techniques, and new ways to improve water uptake and reduce erosion.</p>
<p class="lead">Build mechanisms that will get people out of harm’s way and prepare shelters to protect them when they are forced to move.</p>
