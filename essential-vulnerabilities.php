		          <h3 class="mb-4">Environmental degradation</h3>
            	</div> 
			</div>      
    </div>
<div class="col-md-9 col-lg-5">
	<p class="lead mb-4">
Communities can all too often increase the probability and severity of disasters by destroying the forests, coral reefs and wetlands that might have protected them. 
</p>

<h4 class="mt-4">The facts</h4>
<ul>

<li class="mb-2">Forests once covered 46 per cent of the Earth’s land surface and half of these have disappeared; only one-fifth of the Earth’s forests remain undisturbed.
</li>
<li class="mb-2">Coral reefs are home to one-fourth of all marine species; 60 per cent of coral reefs could be lost in the next 20-40 years.
</li>
<li class="mb-2">The expansion of deserts and the degradation of land threaten nearly one-quarter of the planet’s land surface; more than 250 million people are directly affected by desertification and 1 billion are at risk.
</li>
<li class="mb-2">Global warming will only intensify species loss and ecosystem damage, which will further increase vulnerability to disasters.
</li>
</ul>

<div class="bg-white p-4 mb-4 mt-4">
<h4>Environmental degradation: What can be done?</h4>
<p class="lead">Undertake land-use planning with an ecosystem approach.</p>
<p class="lead">Recognize the risk reduction function of ecosystems in environmental policies and legislation.</p>
<p class="lead">Identify and protect natural buffers such as forests, wetlands and coral reefs.</p>
<p class="lead">Restore forests and plant mangroves to shield communities from hazards such as storm surge, coastal flooding and tropical storms.</p>
<p class="lead">Manage forests to reduce wildfire risk.</p>
