    </div>
<div class="col-md-9 col-lg-5">
	<p class="lead mb-4">
The rapid growth of cities, combined with climate change and the urban population explosion, will create new stresses for urban settlements and make city dwellers increasingly vulnerable.</p>

<h4 class="mt-4">The facts</h4>
<ul>
<li class="mb-2">
One out of every two people now lives in a city; this proportion will go on rising; by 2030, 5 billion of the planet’s expected 8.1 billion populations will be urban.
</li>
<li class="mb-2">One in three of the urban population lives in marginal settlements or crowded slums with inadequate access to clean water, sanitation, schools, transport and other public services.</li>
<li class="mb-2">One city dweller in four lives in absolute poverty; by 2030, two-thirds of humankind will live in cities and three billion in slums.</li> 
<li class="mb-2">Eight of the 10 most populous cities on the planet are vulnerable to earthquakes according to an ADB report in 2008. Similarly, large population centres are often located on the coastline, rendering them vulnerable to floods, storm surges and tsunamis. Ineffective land-use planning, inadequate enforcement of building codes and faulty construction standards put millions at risk. (Source: Shaw et al., Urban Disasters and Resilience in Asia, 2016)</li>
<li class="mb-2">Cities with weak governance and small and medium-sized urban areas are more vulnerable to disasters as they have weaker capacities to manage urban growth, deforestation and destruction of coastal systems.</li>
</ul>

<div class="bg-white p-4 mb-4 mt-4">
<h4>Rapid and unplanned urbanization: What can be done?</h4>
<p class="lead">Have national and local budgets to systematically integrate disaster risk reduction in all aspect of urban planning.</p>
<p class="lead">Plan urbanization and avoid building in risk areas.</p>
<p class="lead">Avoid the development of slums, offering safe lands to low-income families.</p>
<p class="lead">Have safer schools, hospitals, roads, bridges than can withstand any type of hazard.</p>
<p class="lead">Identify high-risk areas, build disaster risk reduction into development programmes and implement effective disaster recovery policies.</p>
<p class="lead">Integrate seismic risk assessment in the construction of buildings in areas exposed to earthquakes.</p>
<p class="lead">Involve people at risk by educating them on disaster risk reduction and in making their own neighbourhoods safer; this effectively empowers people and increases their capacity to respond to disaster.</p>
<p class="lead">Protect communities by installing early warning systems.</p>
<p class="lead">Make warnings more effective with regular drills and increase community ability to foresee, prepare for and cope with disasters.</p>
<p class="lead">Give poor communities access to financial mechanisms to protect houses and incomes.</p>
</div>	



