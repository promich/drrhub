<h6>
HOW TO NAVIGATE THROUGH THE COURSE?
</h6>

<p>
The online course consists of seven blocks of disaster risk reduction knowledge that will help reporters to better report on disaster risks before, when and after disasters happen.
</p>

<h6 class="mt-4">
WHO CAN JOIN?
</h6>

<p>
The seven blocks are open to all journalists working for broadcasting media organisations which have signed up to the World Broadcasting Union (WBU) project programme.
Trainees will receive a registration link and a password via email to attend the training.
</p><p>
The seven information blocks are aimed at three different audience groups.
</p>
<p><strong> 
Group 1: Editors-in-chief</strong>
</p><p><strong> 
Group 2: Broadcasters, engineers and technical staff dealing with early warning messages</strong>
</p><p><strong> 
Group 3: Reporters and broadcasters dealing with news, current affairs, documentaries, education and general programming</strong>
</p><p>
Groups 1 and 3 are welcome to complete ALL modules, but MUST complete information blocks 1, 2, 5 and 6. These blocks have been specifically designed for broadcasters who are reporting on news, current affairs, documentaries and educational programmes. Completion of these information blocks is mandatory in order to receive the final certificate. ( Groups 1 and 3)
</p><p>
Group 2 is welcome to complete ALL modules, but MUST complete information blocks 3 and 4. These blocks have been specifically designed for reporters and other staff who are responsible for ensuring that their broadcaster is able to provide early warning messages according to industry standards.  ( Group 2). Blocks 3 and 4 are mandatory for Group 2 in order to receive the final certificate.
</p><p>
All the blocks contain two types of learning modules:
</p><p>
A number of self-paced video-based modules that can be attended online at the convenience of trainees.   
</p><p>
In addition, a total of 7 sixty minute sessions that will be provided online in a live setting, via Zoom with a live trainer (3 sessions for Group 2 EW trainees and 4 sessions for Group 1 and 3 - news and programme content trainees) that will be arranged at a scheduled time with a trainer and communicated to the trainee ahead of time
</p>
<h6 class="mt-4">
GOING THROUGH THE BLOCKS
</h6>
<p>Each block module is supplemented by 'further reading' links. The 'DRR Media Hub' is always available as an invaluable tool to find further information regarding all aspects of DRR. </p>

<p>
<strong>Block 1</strong> is a 30-minute introductory presentation profiling the international organisations and broadcast unions who are part of this project. All participants should complete this block.
</p><p>
<strong>Block 2</strong> is an overview of Disaster Risk Reduction. It provides a taste of DRR information – the hazards, the terminology, the policies – and is a substantive guide to anyone who is involved in reporting on DRR issues. This block is mandatory for all participants. 
</p><p>
<strong>Block 3</strong> is an introduction to Early Warning Systems and what every broadcaster dealing with early warning messages should know and do before, during and after disasters. Target participants are (Group 2) Broadcasters, engineers and technical staff dealing with early warning messages and warnings.
</p><p>
<strong>Block 4</strong> is an explanation of CAP - the Common Alerting Protocol - and what weather forecast reporters need to know to best communicate early warnings.
Target participants are (Group 2) Broadcasters, engineers and technical staff dealing with early warning messages and warnings.
</p>
<p>
<strong>Block 4B</strong> is an explanation of weather forecasting.
</p>
<p>
<strong>Block 5</strong> is a guide for broadcasters, journalists, producers and content managers on how to include disaster risk reduction stories in their output. It includes tips, suggestions and practical examples relating to News and Current Affairs
content. This block is mandatory for all reporters from Groups 1 and 3
</p><p>
<strong>Block 6</strong> targets the same participants as Block 5 - broadcasters, journalists, producers and content managers and looks at how programmes such as documentaries, education programmes and other genres can use DRR themes and issues to reach their audiences. This block is mandatory for all participants from Groups 1 and 3.
</p>
<h6 class="mt-4">
HOW TO GET THE CERTIFICATE
</h6>
<p>
There is a quiz at the end of Blocks 2, 3, 4, 5, and 6, that relevant participants should take in order to receive their certificate
To receive the final certificate, participants of Group 1 and 3 should achieve an 80% success rate from each quiz in Blocks 2, 5, and 6. Participants of Group 2 should achieve an 80% success rate from each quiz in Blocks 2, 3 and 4.
</p>
