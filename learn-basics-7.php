<p> 
This video summarises key patterns and trends that you should know before reporting on a DRR story. 
</p>
<p>
Let’s begin with which disasters are most deadly.
</p>
<p>
Between 2000 and 2019, disasters killed more than 1.5 million people worldwide.
</p>
<p>
Most deaths were caused by earthquakes, storms, epidemics and floods.
</p>
<p>
Earthquakes claimed more than 721,000 lives - almost half of all disaster deaths for the period.
</p>
<p>
Big disasters can take a heavy toll. The 2004 Indian Ocean tsunami and the 2010 Haiti earthquake each killed more than 200,000 people. 
</p>
<p>
In 2020, the COVID-19 pandemic became the deadliest disaster so far this century, dwarfing other death tolls. 
</p>
<p>
Covid aside, disaster mortality is actually on a downward trend. But economic loss is on a steep upward curve.
</p>
<p>
For every person killed by a disaster, many more are affected – more than 200 million people each year.
</p>
<p>
That’s more than four billion people between 2000 and 2019. Expect that number to be far higher once COVID-19 is factored in.
</p>
<p>
Before coronavirus, we could safely say that climate- and weather-related disasters affected the most people.
</p>
<p>
Remember what we learned about climate change acting as an amplifier? More than 90 percent of disasters between 2000 and 2019 were climatological, hydrological or meteorological.
</p>
<p>
For Climatological, think droughts and wildfires. Hydrological, think floods. Meteorological: heat waves and storms.
</p>
<p>
Next trend: Asia is the continent most at risk.
</p>
<p>
It accounts for 41 percent of all disasters and 85 percent of people affected - some 3.4 billion between 2000 and 2019.
</p>
<p>
Now, perhaps not surprisingly, the poorest suffer the most when disasters hit. That’s true in both developing and developed countries. 
</p>
<p>
Many poor people lack insurance or other means to recover quickly. They often lose their homes and livelihoods. This makes them more vulnerable to the next disaster.
</p>
<p>
Drill down further, and you see that women, children, the elderly and the disabled are among the most vulnerable.
</p>
<p>
Women and children are 14 times more likely to die than men during a disaster.
</p>
<p>
Next, bear in mind that unreported small-scale disasters create long-term impacts. 
</p>
<p>
Every year, countless floods, landslides, fires and storms happen all over the world - but they’re often too minor to be recorded in the international databases. 
</p>
<p>
But that doesn’t mean they aren’t devastating for those involved, and they have a cumulative impact on development.
</p>
<p>
Finally, I leave you with this fact: for every $100 spend on development aid, just 40 cents are invested in preventing disasters.
</p>
<p>
In 2014, UN figures show that the global aid budget was around $135 billion. Of that, just $540 million - or 0.4 percent - went to DRR.
</p>
<p>
We know prevention pays, but the world isn’t paying.
</p>
 
