<p>
Next we turn to the "Phases of Disaster Management".
</p><p>
The Disaster Risk Management process adopted by UNDRR has four phases: Mitigation, Preparedness, Response, and Recovery.
</p><p>
Here we see those four phases depicted as a cycle.
</p><p>
The mitigation phase includes any type of activity that seeks to prevent an emergency, reduce the likelihood of its occurrence, or limit the negative effects of unavoidable threats.
</p><p>
The preparedness phase includes the planning and preparation necessary for responding to an emergency event.
</p><p>
In the response phase the plans and procedures established in the preparedness phase are executed.
</p><p>
The recovery phase focuses on helping the community to at least return to pre-emergency levels of safety and functionality, and ideally to improve on pre-existing levels. (This is the idea behind the "Build Back Better" slogan.)
</p><p>
The Mitigation phase involves activities that seek to prevent an emergency, reduce the likelihood of its occurrence, or limit the negative effects of unavoidable threats.
</p>
<ul>
	<li>
Hazard mapping, vulnerability assessments
</li><li>
Data collection, recording and analysis
</li><li>Awareness raising, education campaigns
</li><li>Development of policies and legislation
</li><li>Development of regulatory frameworks
</li><li>Allocation of financial resources
</li><li>Establishment of public-private partnerships
</li><li>Involvement of multi-stakeholders from different sectors, including media and private sector as well as government
</li>
</ul>
<p>
The Preparedness phase includes the planning and preparation necessary for responding to an emergency.
</p><p>
Agreed plans and procedures are developed to ensure critical operations are maintained during and after the emergency.
</p>
<ul>	
<li>Planning and developing Standard Operating Procedures (SOPs)
</li><li>Implementing Early Warning Systems
</li><li>Capacity building and training exercises
</li><li>Sharing Information and knowledge
</li><li>Leveraging new technologies
</li>
</ul>
<p>
This is the Response phase, initiated by the emergency itself.
</p><p>
During the emergency, the plans and procedures established in the mitigation and preparedness phases are executed.
</p>
<ul>	
<li>Disseminate critical information
</li><li>Deliver warnings, before and during the emergency
</li><li>Provide critical services throughout affected areas
</li><li>Assess damage and needs
</li><li>Support inter-agency coordination
</li>
</ul>
<p>
The Recovery phase occurs after the emergency.
</p><p>
It focuses on providing help needed for the community to meet or exceed pre-emergency levels of safety and functionality.
</p>
<ul>	
<li>Disseminating needed information to affected communities
</li><li>Identifying best practices and lessons learned
</li><li>Restoring public sector operations
</li><li>Restoring all critical infrastructure and services
</li>
</ul>
	
	 
	  
	  
	  
