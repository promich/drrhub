<div class="col-md-9 col-lg-5">
	<p class="lead mb-4">
		In March 2015, UN member states agreed on a 15-year plan to reduce disaster risk by 2030 by endorsing the Sendai Framework for Disaster Risk Reduction 2015-2030, which was adopted at the Third World Conference on Disaster Reduction, in Sendai, Japan.</p>
	<p>	      
The Sendai Framework is a non-binding agreement which succeeds the Hyogo Framework for Action (HFA) 2005-2015. Its goal is to reduce disaster losses by reducing existing levels of disaster risk and preventing the creation of new risk by reducing exposure and vulnerability and increasing preparedness and strengthening resilience.</p>
	<p> 
The Sendai Framework is aligned with the other 2030 Agenda framework agreements, including The Paris Agreement on Climate Change, The Addis Ababa Action Agenda on Financing for Development, the New Urban Agenda and, ultimately, the Sustainable Development Goals.</p>
<p>
It recognizes that the State has the primary role to reduce disaster risk but that responsibility should be shared with other stakeholders including local government, civil society actors, the private sector and others.
</p>
<p>
UNDRR is tasked to support the implementation, follow-up and review of the Sendai Framework.
</p>


