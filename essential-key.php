<h4>Four priorities</h4>
<p>The four priorities of the Sendai Framework indicate several of the roles the media can play in building resilience and contributing to disaster risk reduction. It plays an obvious role with regard to the fourth priority by promoting disaster preparedness among its audiences, but it can work towards other priorities as well. It can, for instance, highlight local risk perceptions and assessments in its coverage so that they come to the attention at the local and national levels, or seek information about risk governance and plans in a manner that promotes public information as well as accountability.</p>



<h4 class="mt-4">Monitoring progress under the Sendai Framework</h4>
<p>Strong accountability is one of the cornerstones of the Sendai Framework for Disaster Risk Reduction. A set of 38 indicators has been identified by the Open-ended Intergovernmental Expert Working Group to track progress across the Sendai Framework’s seven targets and the associated Sustainable Development Goals (SDGs). This set of indicators allows the tracking of progress within each country and region, as well as global trends in DRR.</p>


<p>
The Sendai Framework Monitor is a management tool to help countries develop disaster risk reduction strategies, make risk-informed policy decisions and allocate resources to prevent new disaster risks. It offers journalists a template with which to explore various dimensions of their country’s progress in implementing DRR measures. It can be used to report on legislation and other measures within their countries.
</p>




