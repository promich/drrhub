      	    <div id="audiences" class="row sticky-top">
	      	    <div class="col">
		          <h3 class="mb-4">Audiences</h3>
            	</div> 
			</div>      
            
    </div>
<div class="col-md-9 col-lg-5 mt-sm-2">
	<p class="lead mb-4">
To begin with, it is always good to think about the information needs and interest of your audience. This of course depends on the profile and reach of your media outlet. Audiences are important to journalists, but they can recede into the background in the daily routines of reporting.</p>
<p>
Interacting with your audience and keeping track of what they are talking about or worried about at any time is a good place to begin. Different audiences have different needs and, during a fast-moving event like a disaster, this can change from day to day and often from morning to evening, depending on developments and what information they have received from other sources meanwhile.
</p>
<p>
<strong>Local audiences</strong> need verified and accurate information about the disaster itself, its impact, and rescue and relief efforts. They may need detailed information about risks, developments, counter measures, restrictions, where and when help is available, and local issues, people and places. They also need factual and scientific information that will serve as an antidote to panic.
</p>
<p>
<strong>National audiences</strong>, on the other hand, will not be interested in the same level of detail as audiences in the local area immediately affected by a disaster. International organisations that are planning to participate in the response may be geographically distant but will want to know what others are doing, the exact nature of needs and where to focus their efforts.
</p>

