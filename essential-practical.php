      	    <div id="long" class="row sticky-top">
	      	    <div class="col">
		          <h3 class="mb-4">Long-term DRR stories</h3>
            	</div> 
			</div>      
            
    </div>
<div class="col-md-9 col-lg-5 mt-sm-2">
	<p class="lead mb-4">
You can continue your DRR coverage after the immediate impact of the disaster has receded. During a disaster and its immediate aftermath, you will usually be run off your feet trying to keep up with developments and the news cycle. But it would have served as a valuable opportunity to build contacts with disaster management experts and those in other related fields. Continue to cultivate those sources even when the intense phase of coverage is over and they will yield a rich crop of stories. Examining resilience issues round the year is important to develop your contacts and skills, and also to establish your credibility with experts and audiences alike.</p>

<h4>Finding the right news peg</h4>
<p>Many DRR stories will pass the test of newsworthiness even when there is no disaster in sight, depending on how well you can ‘hang’ your story on a ‘news peg’.</p><p>
Anniversaries of disasters provide an ideal platform for raising DRR issues. DRR stories do not come to you, you must go after them, so these are ideal occasions to make a start and convince your editor that you have a good story to tell.
</p><p>
Some risks and hazards are not marked by single disasters or events. Global warming and climate change are in this category. In such cases, the release of reports and findings, international conferences and events, and connected events such as severe storms in other parts of the world can provide a convenient ‘news peg’ for stories in your area. Media outlets sometimes launch campaigns around such issues, as the British newspaper <em>The Guardian</em> did for climate change which they now habitually refer to as the Climate Emergency.  </p>



<ul class="mt-4">
<li class="mb-2">
Investigate the potential threats and risks that might endanger the lives of populations in your village or country (informal settlements, poor construction in disaster-prone zone and destruction of natural environmental buffers)
</li>
<li class="mb-2">
Investigate the degree of DRR measures undertaken (prevention, mitigation, preparedness, recovery) and how they reduce risk
</li>
<li class="mb-2">
Do not wait for a disaster to happen before writing about potential threats. Be proactive
</li>
<li class="mb-2">
Focus on how communities prepare for disasters e.g. before the rainy season
</li>
<li class="mb-2">
Cover drills, preparedness exercises, education measures and activities to inform people of their risk and vulnerabilities and educate them about what they can do
</li>
<li class="mb-2">
Conduct interviews with experts and disaster management authorities and initiate a possible debate on a DRR issue
</li>
<li class="mb-2">
Develop regular stories on people’s vulnerabilities to disasters, their social, environmental and economic vulnerabilities
</li>
<li class="mb-2">
Report on how the public and governments interact
</li>
<li class="mb-2">
Link any story on the environment, poverty, climate change or urban risk to a disaster risk reduction issue; in other words, report on disasters in the making where vulnerabilities are developing in hazard-prone zones
</li>
<li class="mb-2">
Take any international disaster opportunity to highlight a local or national potential threat
</li>
<li class="mb-2">
Keep the memory of past disasters alive: people tend to forget and react only when disasters happen
</li>
<li class="mb-2">
Commemorate the International Day for Disaster Reduction, which is on 13 October and World Tsunami Awareness Day on November 5
</li>

</ul>

