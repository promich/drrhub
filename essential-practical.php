		          <h3 class="mb-4">Differentiate your content</h3>
            	</div> 
			</div>      
            
    </div>
<div class="col-md-9 col-lg-5 mt-sm-2">
	<p class="lead mb-4">
There is a lot of news coverage when disasters happen, but a lot of this output can be quite similar in content and approach because different media outlets report the same central facts and use a lot of the same sources of information. You can distinguish yourself as a journalist by coming up with story ideas that are different from what everyone else is doing – and using a DRR perspective can give you a wealth of ideas for this.</p>
<p>
You can differentiate yourself from others if you consciously set the agenda when you can, instead of following the agenda set by others. And the way to do this is to look for fresh stories and fresh angles that are about issues relevant to your audiences. This chapter outlines approaches and angles that can be used to produce stories that are both interesting and meaningful.   </p>


