<div class="col-md-9 col-lg-5">
	<p class="lead mb-4">
		<strong>Disaster</strong> is a term used to refer to “a serious disruption of the functioning of a community or a society at any scale”.  It refers to a disruptive event or series of events, not to underlying threats. Disasters lead, among other things, to human, material, economic and environmental losses or impacts. </p>
		<p class="lead mb-4">
Disasters may be termed <strong>small-scale disasters</strong> which affect local communities or <strong>large-scale disasters</strong>, which require a national or international response. They can be described as <strong>frequent</strong> or <strong>infrequent</strong> and they might be <strong>slow-onset disasters</strong> such as a pandemic, drought, desertification or sea-level rise, or <strong>sudden-onset disasters</strong> such as earthquakes, volcanic eruptions, flash floods, chemical explosions, and transport accidents. 
</p>
<p class="lead mb-4">
The term <strong>emergency</strong> is sometimes used interchangeably with the term disaster, as, for example, in the context of biological and technological hazards or health emergencies.</p>
<p class="lead mb-4">
Disasters originate from <strong>hazards</strong> – the underlying conditions, processes, phenomena or activities that can lead to serious disruptions or losses. Hazards could be: 
</p>

<ul>
	<li class="lead mb-2"><strong>Biological</strong> (bacteria, viruses, parasites, venomous wildlife or insects, poisonous plants and mosquitoes carrying disease-causing agents, among others)</li>
	<li class="lead mb-2"><strong>Environmental</strong> (including soil degradation, deforestation, loss of biodiversity, salinization and sea-level rise)</li>
	<li class="lead mb-2"><strong>Geological</strong> (earthquakes, volcanic activity and emissions, landslides, rockslides, surface collapses, etc.)</li>
	<li class="lead mb-2"><strong>Hydrometeorological</strong> (cyclones, floods, flash floods, drought, heatwaves and cold spells, and coastal storm surges, among others), or</li>
	<li class="lead mb-2"><strong>Technological</strong> (e.g. industrial pollution, nuclear radiation, toxic wastes, dam failures, transport accidents, factory explosions, fires and chemical spills). </li>
</ul>	

<p class="lead mb-4"><strong>Multi-hazard</strong> conditions often exist, and when hazards cascade, they can lead to a large-scale disaster. An example is when a geological hazard such as an underwater earthquake leads to a tsunami, which in turn leads to an industrial accident with a radiological hazard, as happened during the Fukushima Daiichi nuclear disaster in Japan in 2011. Multi-hazard conditions are quite common – earthquakes often lead to landslides, droughts lead to famines, storms lead to cold waves or floods, heat waves lead to wildfires, floods lead to dam bursts, and so on.</p>

