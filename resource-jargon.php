<div class="col-md-9 col-lg-5">
	<p class="lead mb-4">
		<strong>Disaster risk</strong> is defined as the totality of risk that a disaster poses to a system, society or community in terms of potential loss of life, injury, or destroyed or damaged assets. It comprises different types of potential losses which are often difficult to quantify, but which can at least be assessed in broad terms. Risks are sometimes conceived of as <strong>intensive</strong> or <strong>extensive disaster risks</strong>. The former term refers to disasters that are high-severity but mid- to low-frequency, mainly associated with major hazards such as strong earthquakes, active volcanoes, tsunamis or major storms. The latter term refers to the risk of low-severity, high-frequency disasters such as recurring localized floods, landslides and storms</p>

<p class="lead mb-4">
Risk is often understood as a function of hazard, exposure, vulnerability and capacity. <strong>Exposure</strong> represents the assets that might be impacted by a hazard, including the situation of people, infrastructure, housing, production capacities and other tangible human assets.
</p>

<p class="lead mb-4">
<strong>Vulnerability</strong> is the susceptibility of these assets, or the degree to which someone or something can be affected by a particular hazard. Vulnerability could be rooted in a number of factors, including physical (e.g. unstable locations, closer proximity to hazards), economic (poor pay, no savings and insurance, etc.), social (low status in society, fewer decision-making possibilities, oppressive formal and informal institutional structures) psychological (fears, ideologies, political pressures, mental illness) and physiological factors (age, mobility, disability, etc.). </p>

<p class="lead mb-4">
<strong>Capacity</strong> is the combination of all the strengths, attributes and resources available to manage and reduce disaster risks and strengthen resilience – and of course the media plays an important role in building disaster resilience, whether by communicating key messages including early warnings, creating awareness, providing information or reassurance, exposing vulnerabilities, focusing attention on specific problems or promoting accountability.
</p>

<p class="lead mb-4">
It is important to recognise <strong>underlying disaster risk drivers</strong> – factors that increase levels of exposure and vulnerability, such as poverty, inequality, climate change, unplanned urbanization, environmental degradation, demographic change and lack of regulations. </p>

<p class="lead mb-4">
<strong>Acceptable risk</strong> or <strong>tolerable risk</strong> are terms which refers to the extent to which a disaster risk can be deemed acceptable or tolerable, and it depends on existing social, economic, political, cultural, technical and environmental conditions. In simplistic terms, losing $5 might be an acceptable risk but losing $500 might not be. <strong>Residual risk</strong> is the risk that remains even after effective disaster risk reduction measures are in place, and for which emergency response and recovery capacities must be maintained. </p>

</div>

