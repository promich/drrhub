<div class="col-md-9 col-lg-5">
	<p class="lead mb-4">
		A number of different terms are used to convey various interlinked concepts in the risk reduction, risk management, mitigation, disaster prevention, planning and resilience. Not only are the terms important to understanding the nuances of expert opinion, they provide an insight into the different activities and processes that are involved.</p>

<p class="lead mb-4">
<strong>Disaster risk reduction</strong> is aimed at preventing new disaster risks, reducing existing risks and managing residual risk, all of which contribute to strengthening resilience and therefore to the achievement of sustainable development. It is the policy objective of disaster risk management, and its goals and objectives are defined in disaster risk reduction strategies and plans.
</p>

<p class="lead mb-4">
<strong>Disaster risk reduction strategies and policies</strong> define goals and objectives across different timescales and with concrete targets, indicators and time frames. The United Nations has endorsed the Sendai Framework for Disaster Risk Reduction 2015-2030 as the overarching framework for achieving a substantial reduction of disaster risk and losses.
 </p>

<p class="lead mb-4">
<strong>Local knowledge</strong> is increasingly considered critical for the assessment of hazards, vulnerabilities and capacities. It is important to promote <strong>community-based disaster risk management</strong> from planning through to implementation, monitoring and evaluation of local action for disaster risk reduction. The value of <strong>local and indigenous peoples’ approach to disaster risk management</strong> is also recognised as central to disaster risk management.</p>

<p class="lead mb-4">
<strong>Prevention</strong> is the term used for activities and measures to avoid existing and new disaster risks by reducing vulnerability and exposure. Examples include dams or embankments to eliminate flood risks, land-use regulations to prevent settlement in high-risk zones, seismic engineering design, and immunization.</p>

<p class="lead mb-4"> 
<strong>Mitigation</strong> means the lessening or minimizing of the adverse impacts of a hazardous event. Mitigation measures include engineering techniques and hazard-resistant construction as well as improved environmental and social policies and public awareness. It should be noted that, in climate change policy, “mitigation” is defined differently, and is the term used for the reduction of greenhouse gas emissions that are the source of climate change.</p>
<p class="lead mb-4"> 
<strong>Resilience</strong> is the ability of a system, community or society to resist, adapt to, transform and recover from a disaster in a timely and efficient manner. Resilience is developed through a series of processes that includes recognising risks, mitigating them, planning and building capacity to cope with the disruption of disasters. The media play an important role in building resilience.</p> 
<p class="lead mb-4"> 
<strong>Structural and non-structural measures</strong> may be implemented to reduce or avoid the possible impacts of hazards. Structural measures include physical constructions such as dams or shelters and the application of engineering techniques or technology to achieve hazard resistance and resilience. Non-structural measures are measures which use knowledge, practice or agreement, through policies and laws, public awareness raising, training and education.</p>





