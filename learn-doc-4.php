	  <p>You could say that all programming involved with DRR is "educational" in that the information or knowledge being offered is trying to educate an audience but when we spoke earlier about the roles and responsibilities of broadcasters we didn't particularly target how we can educate - not just adults, but young people and whole communities.</p><p>
Across the globe, young people are becoming more and more involved in climate related issues and by association learning about reducing the risks associated with disasters is a part of that generational process. It makes sense therefore for broadcasters to work with schools and youth organisations on DRR projects that young people can contribute to and be a part of.</p><p>
For example, any broadcaster can devise a set of competitions where schools take part in practical exercises... they observe their own communities where hazards  (natural or man-made) have occurred and they then suggest ways to avoid or minimise risks in the future... such as escape routes, emergency evacuation drills, ways to communicate with the elderly or work with less able people. Sometimes involving a celebrity or a sports star generates an enthusiasm and an innovation that only a younger generation can create. These competitions often turn into entertaining television but they ultimately benefit communities.</p><p>
The Japanese broadcaster NHK have devised all sorts of programmes and videos to encourage educational broadcasting that cross generations and their BOSAI project for example, is well worth looking at as a guide to what's possible.</p><p>
The other source of programme material is the work of NGO's. The obvious port of call is the Red Cross or Red Crescent who are active across the globe with training schedules, mock exercises, volunteer projects and who produce excellent video material to supplement your own local footage and interviews. But by mapping the numerous organisations in your area who are part of the DRR scene, you'll invariably find a wealth of activities that are a source for programme ideas... it takes time and  research - yes - but the results are often surprising.</p> 	  
	  
