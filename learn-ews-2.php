<p>
Geophysical disasters originate from activity of the Earth.
</p><p>
Earthquakes can occur on land or under the seabed. Earthquakes occurring below water can generate large waves known as tsunami.
</p><p>
Another hazard type that can lead to a disaster of geophysical origin is the downward movement of large amounts of ground material. These hazards include avalanches, landslides and rock falls.
</p><p>
Volcanoes are places on the Earth's surface from which lava, ash, and gasses are emitted.
</p><p>
Hydrological disasters are those caused by changes in the movement and distribution of surface and subsurface water.
</p><p>
Such hazard threats can result in coastal floods (typically caused by storms) and river floods (due to sudden, heavy rainfall, usually associated with temporary weather events).
</p><p>
There are also ice jam floods, defined as the accumulation of floating ice restricting or blocking a river's flow and drainage.
</p><p>
Meteorological disasters are hazards caused by short-lived, extreme weather and atmospheric conditions that last from minutes to days.
</p><p>
These include: fog, extreme temperatures (heat waves, cold waves), severe winter conditions, and severe storms. Storms occur on scales ranging from tornadoes, around 1 kilometer  across, up to extratropical cyclones that can be as large as 3,000 kilometers across.
</p><p>
A sixth category, "Extraterrestrial", refers to asteroid impacts and disturbances in the Earth-Sun system, such as a solar storm, or a "Coronal Mass Ejection". These hazards are called "Space Weather".
</p><p>
Climatological disasters refer to those caused by long-lived atmospheric processes causing climate variability on a time scale from within a season to spanning decades.
</p><p>
Climatological disasters include droughts and wildfires.
</p><p>
A drought can be defined as a "prolonged absence or marked deficiency of precipitation," resulting in crop damage or a scarcity of water used by people, animals or plants.
</p><p>
Wildfires are defined as "any uncontrolled and non-prescribed combustion or burning of plants in a natural setting such as a forest, grassland, brush land or tundra, which consumes natural fuels and spreads based on environmental conditions, such as wind."
</p><p>
Biological disasters comprise the last type caused by natural hazards. 
</p><p>
They  might occur naturally, by the exposure to living organisms and their toxic substances, or by vector-borne diseases that they may carry. Such hazards include insect infestations and epidemics produced by infections.
</p><p>
Biological disasters can also be caused by humans, deliberately or accidentally.
</p><p>
Technological disasters are those caused by hazards of human origin, such as industrial, transport, or other types of accidents, classified as miscellaneous
</p><p>
Industrial accidents include chemical spills, collapse of infrastructure, explosion, fire, gas leaks, poisoning, or radiation, among others.
</p><p>
Transport accidents can be classified by the type of transportation, such as air, rail, road or water.
</p> 	  
	  
	  
