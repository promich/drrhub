<p>

Natural hazards turn into disasters when lives and livelihoods are destroyed. When a weather or climate event turns severe and extreme it becomes a natural hazard.
</p><p>
Some regions of the globe are more susceptible or vulnerable to natural hazards than others but they occur across the globe.
</p><p>
Natural hazards include air pollution, tsunamis, landslides, and mudslides, desert Locusts, forest and wild fires, volcanic ash, avalanches, dust storms and sandstorms.
</p><p>
Air pollution
</p><p>
Smoke from large fires, ash and other emissions from volcanic eruptions, and chemical releases from industrial accidents. After the Chernobyl nuclear power plant accident in 1986 there was urgent need for emergency response to track chemical releases from that industrial accident. National weather services through the WMO now use specialized atmospheric dispersion-modelling techniques to track and predict the spread of airborne hazardous substances in the event of an environmental emergency.  
</p><p>
WMO monitors air pollution through the Global Atmosphere Watch programme. Reliable scientific data and information on aerosols helps to track and forecast aerosols which also more accurately estimate greenhouse gas emissions. Air quality warnings support decision-makers in many sectors for example health and agriculture.
</p><p>
Dust storms
</p><p>
Sand and dust storms are common meteorological hazards in the world’s desert regions. Arid and semi-arid regions cause intensely hot air to destabilize the lower atmosphere creating thunderstorms which increase wind speed over a wide area.
</p><p>
These strong winds lift vast amounts of sand and dust from bare, dry soils into the atmosphere, transporting them hundreds to thousands of kilometres away from Africa
to the Americas. Some 40% of aerosols travelling through the lowest layer of Earth’s atmosphere are dust particles from wind erosion.
</p><p>

WMO Sand and Dust Storm Warning System issues sandstorm and dust storms warnings through regional centres: 
</p><p>
Northern Africa-Middle East-Europe - State Meteorological Agency AEMET in Barcelona, Spain, 
</p><p>
Asia -China Meteorological Administration in Beijing China, and
</p><p>
the Americas - Caribbean Institute for Meteorology and Hydrology in Barbados. Barbados hosts the longest continuous observation of Saharan dust at Ragged Point.
</p><p>
Wildfires
</p><p>
Fire is important to keep most ecosystems healthy. However, wildfire can also present an urgent threat to life and property where wildland meets homes and and those fighting fires make decisions based on expert meteorological advice.
</p><p>
Accurate fire weather forecasts and warnings spanning minutes out to months in the future are used by land and fire managers for planning and fire suppression purposes. Forest and Wildland fires can be devastating and they're often triggered during or after periods of drought by lightning or sometimes by human actions.
</p><p>
Desert locusts 
</p><p>
Desert locusts migrate across Africa, the Middle East, Asia and southern Europe and swarms can travel up to 200 kilometres a day. Consuming as much food in one day that can feed 2500 people - leading to humanitarian disaster.
</p><p>
In Africa locust breeding is associated with seasonal rains. Successive rains over large areas favour increased numbers. Temperature, air moisture, soil moisture, day length and vegetation are key factors that directly affect how fast locusts breed, size and their ability to spread. Swarms often gather where the trade winds meet.
</p><p>
The World Meteorological Organization through its regional centres and NMHS in Africa forecasts seasonal rainfall and flood patterns linked to the Inter Tropical Convergence Zone, soil moisture, wind direction and speed.
</p><p>
In Venezuela in 1999, after two weeks of continuous rain, landslides and mudflows slid down a mountain, destroying towns and causing an estimated 15 000 fatalities.
</p><p>
Mudslides and landslides are usually unexpected and local events. Heavy rain or rapid snow or ice melt or overflowing crater lake can loosen soil and rock, mud on steep slopes that eventually slide downhill reaching speeds of a 50 kilometres per hour.
</p><p>
Avalanches are seasonal hazards which occur when a mass of snow and ice falling suddenly down a mountain slope, often taking earth, rocks and rubble with it. Avalanches can be highly destructive, moving at speeds in excess of 150 km/h.

