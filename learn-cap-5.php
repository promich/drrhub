<p>
We have seen that CAP can help you gather key facts of an emergency situation. But, you also need to know those facts are right.
</p><p>
How can you know which alerting sources are officially recognized as "authoritative"?
</p><p>
Alerting more and more is relying on large public networks like the Internet. At large scales, it can be impossible to know your sources personally, as might have been the case in a small town.
</p><p>
The international Register of Alerting Authorities fills that knowledge gap Each entry in the Register asserts a particular alerting source as authoritative.
</p><p>
The international Register of Alerting Authorities was set up like a referral service--you have a degree of trust in a registered alerting authority because you trust the service that registered them.
</p><p>
This is the first page of the international Register of Alerting Authorities.
</p><p>
This screen shot shows one alerting authority of the United States, NOAAâ€™s National Weather Service.
</p><p>
We see the hazard categories for which this authority typically issues alerts: Geo, Met, Fire, Health, Environment, and CBRNE (Chemical, Biological, Radiological, Nuclear or high-yield explosive).
</p><p>
On the map we see the typical alerting area for this alerting authority.
</p><p>
In this case, there is a CAP news feed URL.
</p><p>
If we follow that URL, we see here the CAP alerts disseminated by the U.S. National Weather Service as Internet news feeds. There are thousands of these CAP news feeds from just the U.S. National Weather Service.
</p><p>
What kind of organization is a typical alerting authority? At the national level, an official alerting authority could be:
</p>
<ul>
	<li>
	a National Meteorological or Hydrological Service like the  U.S. National Weather Service)
	</li><li>
	an Emergency Management Agency, a National Disaster Management Organization, or
	</li><li>
	any other nationally authorized organization.
	</li>
</ul>
<p>
Actually, an "alerting authority" can be any organization officially authorized to perform public alerting.
</p><p>
Different countries have their own policies on what it means to be "officially authorized". But , there is agreement that official alerting authorities should be known internationally.
</p><p>
The international Register of Alerting Authorities was set up by the World Meteorological Organization (WMO) and the International Telecommunication Union (ITU).
</p><p>
Each WMO Permanent Representative (PR) maintains entries for that nation. The PR represents the entire nation and should register all nationally recognized alerting authorities.
</p><p>
Right now, there are 276 listed authorities.
</p>
