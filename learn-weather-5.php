	  <p>
WARNINGS 
	  </p>
	  <p>
A weather warning is a meteorological message issued to provide appropriate warnings of hazardous weather conditions. Warnings of extreme weather events support disaster risk management.
	  </p>
	  <p>
There are three components to the Integrated Warning System, designed to reduce or eliminate the impact on people and property from any hazard:	  </p>
	  <ol><li>Firstly, <strong>Forecast, Detection and Warning</strong>; </li>
		  <li>Secondly, <strong>Dissemination and Communication</strong>; and </li>
		  <li>Thirdly, <strong>Response</strong>.</li>
	  </ol>
	  <p>
Dissemination is the delivery of the warning message. However, communication is achieved only after the information is received and understood.
	  </p>
	  <p>
Let's take a look at the <strong>Warning Cycle</strong>.
	  </p>
	  <p>
Warnings are initiated, updated, corrected, cancelled and retired as soon as the need arises – and renewed as required to keep up with changes in the nature of the threat. 
	  </p>
	  <p>
For example, are there changes in the intensity or duration of the hydroMet hazard? Are strong winds increasing or decreasing? Is the duration of the heavy rains set to lengthen or shorten?
	  </p>
	  <p>
Public weather warnings also depend on the available modes of communication. And the needs and expectations of the community, the media and emergency services.
	  </p>
	  <p>
When a National Meteorological hydrological Service issues a warning the forecasters put together a <strong>warning message</strong>.
They keep messages short and simple: Three lines maximum to a sentence. No more than 15 lines of text. Get it across in 60 seconds or less. Just like a news package.
	  </p>
	  <p>
First, a heading that must ‘stand-alone’ and ‘stand out’ and clearly indicate the fact that it is a warning. It should accurately define the type of event and emphasize the severity of the event for example “Severe Thunderstorm Warning”.
	  </p>
	  <p>
A “headline” which summarizes the most important message followed by non-technical information for public safety. Technical information with supporting detail. Appropriate protective action statements.
	  </p>
	  <p>
Pay attention to the following components:
	  </p>
<ul>
	<li>
Date and time of issue.
	</li>
	<li>
Issuing source or authority and how long the warning is in effect.
	</li>
	<li>
A  Synopsis tells the stage in development of the weather event and also tells you what actions are recommended from the appropriate authority.
	</li>
	<li>
Pay attention to the Current intensity, location and movement of weather hazard.
	</li>
	<li>
Forecast of probable development - intensity, location and movement of that hazard most likely impact area(s) and expected consequences.
	</li>
	<li>
The Level of uncertainty or probability of event occurring. Protective action statements (with appropriate attribution). Source or sources of further information, which is very important to you broadcasters.
	</li>

</ul>


