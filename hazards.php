<div class="bg-white p-4">
<h4>Landslides: Disaster risk reduction measures</h4>
<p>Have a hazard map to identify landslides risk and vulnerabilities.</p>
<p>Have a monitoring system in place to observe and alert when landslides happen.</p>
<p>Integrate landslide risk assessment into urban planning strategies.</p>
<p>Develop new building codes and standards, emphasizing the use of building materials to reinforce infrastructure resilience to landslides.</p>
<p>Implement structural measures such as providing practical stabilization of hazardous slopes, redesigning river protection to reduce erosion and modifying the geometrical characteristic of the slope</p>
<p>Secure towns, villages and tunnels at the bottom of slopes with concrete retaining walls and protection.</p>
<p>Reinforce river protection with wooden dams of limited height in streambeds with potential debris flow.</p>
<p>Improve drainage, building tunnels and trenches to stabilize slopes.</p>
<p>Protect forest cover and ban logging.</p>
<p>Educate people and raise awareness of landslide risk.</p>
<p>Develop an early warning system to measure rainfall levels, with regular drills and evacuation exercises and ensure community participation.</p>
<p>Have a contingency plan in place at a national and local level for people to evacuate in time.</p>
