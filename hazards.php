<p>Make sure that hospitals and health care can continue working when they are most needed.</p>
<p>Build resilient infrastructure.</p>
<p>Assess potential risks before planning and building hospital.</p>
<p>Have a hazard map to identify people at risk and their vulnerability.</p>
<p>Have a national or local plan in place to plan and anticipate.</p>
<p>Train staff on potential risks.</p>
<p>Install a monitoring system to predict and proceed to early evacuation.</p>
<p>Ensure contingency and response plans are in place at a national and local level to evacuate people on time.</p>
<p>Educate people and raise awareness on potential risk.</p>
