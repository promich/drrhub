<h4>Earthquakes: Disaster risk reduction measures</h4>

<p>Integrating seismic risk into land-use planning and urban development strategies in earthquake-prone zones.</p>
<p>Ensuring that building codes are enforced in critical high-use and high-occupancy infrastructure (hospitals, schools, housing, factories) in earthquake zones to prevent buildings from collapse and maintain continuity of basic services.</p>
<p>Having a warning system in place to cut off gas and electricity supplies to reduce fire risk. </p>
<p>Improving education and awareness through training and preparedness programmes in schools and workplaces on the importance of building safety.</p>
