    <div class="col-md-7">
	    <p class="lead">
		 Technological systems are complex, with many dependent subsystems. The failure of one element within this system can cascade throughout the chain, causing a series of failures leading to a disaster. Technological hazards are increasing due to the scope of technological expansion. They include industrial activity that includes dangerous conditions, processes, chemicals or toxic materials, all transport systems (land, sea, air), defensive or offensive weapons systems, nuclear materials in civilian facilities such as power plants, and the misuse of chemical or nuclear weapons. </p>
		 
		 
		 <p class="mb-4">
A new set of emerging technological risks under the Sendai Framework include ICT-related hazards. The increasing dependence upon complex large-scale network architectures of information technologies also increases exposure to cyber security threats. These threats include computer viruses, worms, Trojan horses, malware, spoofing attacks, identity theft, the theft and illegal disclosure of data, the loss of data and contamination of data. They have the potential to disrupt essential infrastructure operations such as communication, health, banking, transportation, energy, education and many other services.</p>

<h3>Risk factors</h3>
<p class="lead">
In many countries, industrial hazards are exacerbated by ageing, abandoned or idle installations. These problems are amplified by insufficient institutional and legal capacities to deal with technological risk reduction. 
</p>
<p class="mb-4">
Natural hazards – for example, storms, landslides, floods or earthquakes – can also cause industrial accidents by triggering the release of hazardous substances from industrial facilities that are located within their path of destruction. The cascading effect of natural hazards triggering technological disasters are called NATECH accidents. The Great East Japan earthquake and tsunami and subsequent Fukushima Daiichi nuclear accident in 2011 put NATECH risks on the global agenda. NATECH events are a recurring but often overlooked feature in many disaster situations. Instruments for reducing technological risks, such as chemical accident prevention and preparedness programmes, often tend to overlook the specific drivers of NATECH events, leaving an important gap in managing this type of risk. </p>

<h3>Vulnerable areas</h3>
<p class="lead">
Most industrial accidents entail the release of hazardous substances into the atmosphere or water bodies with grave impacts on health and human safety. Residential communities around industrial establishments tend to be most at risk because of their proximity, as in the case of Bhopal disaster in India in 1984 where a gas leak from a pesticide plant resulted in more than 15,000 deaths and more than 100,000 people affected from the areas around the facility. 
</p>
<p class="mb-4">
Another example is the collapse of two tailing management facilities (TMFs) – essentially large dams storing chemical waste – in Bento Rodrigues, Brazil, in 2015. A toxic slick comprised of 40 million m3 of waste laden with heavy metals swept 650 km down the Doce River basin, flooding villages downstream, causing 19 deaths and contaminating 2,200 ha of land. The disaster revealed critical gaps in regulation, monitoring, enforcement, information flow, early warning, response and coordination mechanisms between the operator and authorities at all levels. In early January 2019, another TMF dam failure in Brumadinho, Brazil, caused the death of 186 people, with a further 122 missing. The ecosystem of the affected areas is believed to have been damaged permanently. 
	          </p>

<div class="bg-white p-4">
<h4>Technological disasters: Disaster risk reduction measures</h4>
<p>Assess the risks before planning and building critical infrastructure, especially where hazardous substances or processes are involved as in a chemical factory or a nuclear power plant.</p>
<p>Integrate the risks into the planning, foresee and reduce the cascading effects.</p>
<p>Have a hazard map to identify people at risk and their vulnerability.</p>
<p>Have a national or local plan in place to plan and anticipate.</p>
<p>Install a monitoring system to predict and proceed to early evacuation.</p>
<p>Ensure contingency and response plans are in place at a national and local level to evacuate people on time.</p>
<p>Educate people and raise awareness on potential risk.</p>
</div>

