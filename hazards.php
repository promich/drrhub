    <div class="col-md-7 ">
	    <p class="lead mb-4">
		          The Pacific is by far the most active tsunami zone, according to the US National Oceanic and Atmospheric Administration (NOAA), but tsunamis have been generated in the Caribbean and Mediterranean seas, and the Indian and Atlantic oceans.</p>
		          <p class="lead">
The term tsunami comes from the Japanese word for “harbour wave” and is a series of giant, long ocean waves created by an underwater disturbance such as an earthquake, landslide, volcanic eruption or meteorite; it consists of a series of 10 or more waves with crests arriving every 10 to 60 minutes. Tsunami waves can be very long (as much as 100 km) and tens or even hundreds of metres high. They can cross entire oceans without great loss of energy. The Indian Ocean tsunami travelled nearly 5,000 km to Africa, arriving with enough force to kill people and destroy property.</p>

<figure class="figure">
    <img src="assets/img/fig14-tsunami.png" class="figure-img img-fluid" alt="">
  <figcaption class="figure-caption">Figure 14: Tsunami wave height. Selected tsunami wave heights (maximum wave heights recorded). Sources: National Oceanic and Atmospheric Administration National Geophysical Data Center/World Data Service Global Historical Tsunami Database 2019; National Centers for Environmental Information 2019.</figcaption>
