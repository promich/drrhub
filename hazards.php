	    <p class="lead mb-4">
		    Until the arrival of COVID-19, earthquakes were the natural hazard that caused most deaths per event. EM-DAT figures shows that earthquakes claimed more than 720,000 lives between 2000 and 2019. More than 3 billion people live in regions prone to earthquakes.</p>
<p class="lead mb-4">
Most earthquakes are caused by the movement of the earth’s 15 tectonic plates. Geophysicists can identify places where earthquakes are sure to happen, but nobody can predict when an earthquake will happen, or its severity. Seismologists register more than 30,000 tremors every year, but most of these are of low magnitude.</p>


<h3>Risk factors</h3>
<p class="lead">
Many factors aggravate earthquake risks, including:</p>
<ul class="mb-4">
<li class="lead">Population density - eight out of the 10 most populated cities in the world are prone to earthquakes. Most of the world’s earthquakes occur around the Pacific Rim and many of these cities are in Asia where two-thirds of the world’s population lives.</li>
<li class="lead">Poorly built and non-engineered buildings.</li>
<li class="lead">Poverty – constrains more people to live in crowded, sub-standard housing and unsafe places. Earthquakes hurt the poor more than the rich, because education and high building standards can save lives, whereas ignorance and corruption can cost lives.</li>	
</ul>	


<h3>The Richter Scale</h3>
<p class="lead mb-4">
The ranking system used to measure earthquake magnitudes is called the Richter Scale. Developed by Charles Richter in 1935, this scale is used to measure earthquake magnitude. It indicates the energy released by an earthquake. Another ranking system, the Modified Mercalli Intensity Scale, measures seismic intensity. The magnitude of an earthquake is a measured value of the earthquake size. The intensity of an earthquake is a measure of the shaking created by the earthquake; this value varies with location.
	          </p>
