    <div class="col-md-7 ">
	    <p class="lead mb-4">
		    There are more than 1,500 volcanoes potentially active in the world and more than one million volcanic vents under the sea; about 50-60 volcanoes erupt every year worldwide.</p>
<p class="lead mb-4">
A volcano is an opening, or rupture, in the planet’s crust, which allows hot, molten rock, ash and gases to escape from deep below the surface. Volcanic eruptions can be ranked along a spectrum from quiet (effusive) to violent activity, from non-explosive, slow-moving lava flows to explosive eruptions that blast material into the air. The violence of the eruption is determined mainly by the amounts and rate of effervescence of the gases and by the viscosity of the magma itself.</p>
<p class="lead mb-4">
Volcanoes produce a wide variety of natural hazards that can kill people and cause huge economic losses. Avalanches generated by large masses of volcanic cone sliding into the sea can trigger a tsunami. Compared to other natural hazards, such as earthquakes, volcanic eruptions cause generally fewer deaths as eruptions are often predictable and people can be evacuated in time.</p>

<h3>Risk factors</h3>
<p class="lead mb-4">
Although recent decades have seen remarkable progress in monitoring active volcanoes, volcano risk is increasing due to rapid urbanization and the high density of populations living on volcano slopes and valleys. About 500 million people worldwide are exposed to volcano risk and more than 60 large cities are located near active volcanoes. Volcanoes with a high hazard potential are located mainly in developing countries around the circum-Pacific belt (part of Asia, Latin America, the Caribbean and Southwest Pacific.)
 </p>

<h3>Vulnerable areas</h3>
<p class="lead mb-4">
Populations living close to a volcano with no monitoring and early warning systems are the most vulnerable to volcano eruption. Poor people are among the most vulnerable as they are often economically constrained to live in high-risk zones such as on the slopes of an active volcano or in nearby valleys and less prepared to cope with disasters.</p>
<p class="lead mb-4">
People living near volcanoes will be the most vulnerable and forced to abandon their land and homes, sometimes forever. People living far away from the eruption can be affected as their cities and towns, crops, industrial plants, transportation systems, and electrical grids will be damaged by tempura, ash, lahars and flooding.

	          </p>

<div class="bg-white p-4">
<h4>Volcanoes: Disaster risk reduction measures</h4>

<p>Have a hazard map to identify volcano risk and vulnerability.</p>
<p>Install a monitoring system to observe the behaviour of a volcano to predict eruptions and proceed to early evacuation.</p>
<p>Integrate volcano risk in land-use planning: volcano risk can be substantially reduced by limiting the development of infrastructure in hazardous areas.</p>
<p>Ensure contingency and response plans are in place at a national and local level to evacuate people on time.</p>
<p>Educate people and raise awareness on volcano risk.</p>

