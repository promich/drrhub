    <div class="col-md-7">
	    <p class="lead mb-4">
		    Asia is the continent most affected by landslides; the Americas suffer more deaths, and Europe bears the most economic losses with an average economic loss of almost US$ 23 million per landslide.</p><p class="lead mb-4">
Landslides can reach speeds of over 50 km/h and can bury, crush or carry away people, objects and buildings. Landslides cannot be predicted but warning systems measuring rainfall levels can provide warning to people living in landslide-prone areas.</p><p class="lead mb-4">

Landslides can be triggered by geological and physical causes such as glacier or snow melts, heavy rains and water pressure, earthquakes, volcanic eruptions and overly steep slopes. Landslides can also be triggered by human action, the most common being building on unstable slopes. Submarine landslides, or massive slides and rock falls hitting the sea can also cause tsunamis.</p>

<h3>Risk factors</h3>
<p class="lead mb-4">
Population growth, rapid urbanization and environmental degradation (deforestation and inappropriate use of lands and slopes) are increasing landslide risk and exposing more people to landslide danger. High population density, heavy rainfall and rapid land use changes increase the instability of slopes, making people more vulnerable to landslides. </p>
