		          Thousands of avalanches occur every year, killing an average of 500 people worldwide. Avalanches occur when massive slabs of snow break loose from a mountainside and shatter like broken glass as they race downhill. These moving masses can reach speeds of 130 km/hour within about five seconds. 
They cannot always be predicted but they are linked to weather conditions that can be forecast in advance. Most accidents now occur because people ignore warnings. Around 95 per cent of all avalanche incidents are due to slab avalanches with skiers involved.</p>

<h3>Risk factors</h3>
<p class="lead mb-4">
