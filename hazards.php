<h4>Hurricanes, tropical cyclones and typhoons: Disaster risk reduction measures</h4>

<p>Have an early warning system in place to evacuate people on time. Tropical cyclones, hurricanes and typhoons can nowadays be predicted five days in advance and having an early warning system in place is the best way to protect people. This technology is nevertheless not enough if drills and evacuation exercises are not undertaken in a regular manner to ensure full community participation and immediate response.</p>
<p>Structural measures to withstand or lessen the impact of winds and flooding can reduce the damage caused by tropical cyclones.</p>
<p>
Flood risk assessment should be integrated into urban planning strategies.</p>
<p>
People should avoid building directly on the coastline, where hurricane waves may cause flooding and destroy the building.</p>
<p>
Wind-proof buildings are needed to provide community shelter during hurricanes.</p>
<p>
Builders should use flood-resistant material, such as concrete, ceramic or brick.</p>
<p>
Sea walls and revetments could help protect the shore from storm waves.</p>
<p>
Mangroves and trees and coral reefs should be protected as they act as natural wave breakers; wetlands and forests can serve as flood resilient systems, storing large amounts of floodwater, and should also be preserved.</p>
<p>
Raise community awareness, and prepare and practice community evacuation plans, not forgetting the very young, older persons and persons living with disability.</p>
<p>
Incorporate education on hurricanes and protection from hurricane damage in school and social activities.</p>
<p>
Protect and evacuate animals.</p>

