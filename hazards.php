	    <p class="lead">
		    Biological disasters are caused by hazards of organic origin, including bacteria, viruses, parasites, mosquitoes carrying disease-causing agents, and toxins or bioactive substances that occur naturally or are deliberately on unintentionally released. These hazards can lead to economic and environmental damage and loss of life, affecting people and animals at the population level as well as crops, livestock and endangered species of flora and fauna.</p> 
<p class="mb-4">
Epidemic diseases infect millions every year, and the COVID-19 pandemic illustrates the breadth and depth of the transformative impact of biological disasters. The pandemic cost more than a million lives in less than a year, caused widespread disruption to daily life across the world and sparked the deepest economic recession in decades. The 21st century has already experienced several major infectious disease epidemics – old diseases such as cholera and plague have returned, and new ones like severe acute respiratory syndrome (SARS), Middle East respiratory syndrome (MERS), and H1N1 pandemic influenza have emerged. Further epidemics and pandemics are almost certain; the only unknowns are when and where a new lethal threat will emerge. Examples of other recent outbreaks, epidemics or pandemics include Ebola in the Democratic Republic of Congo (2018–2020) and West Africa (2013–2016), and the Zika virus in the Americas and Pacific regions (2015–2016).
</p>

<h3>Risk factors</h3>
<p class="lead">
Biological disasters are driven by a complex set of factors ranging from the ease of spread of biological hazards to people’s exposure to those hazards, their susceptibility to becoming infected and the capacity of individuals, communities, countries and international actors to reduce risks and manage the consequences of outbreaks. The COVID-19 pandemic showed the need for international cooperation and comprehensive multi-dimensional policy responses incorporating health facilities, social management and messaging, economic interventions, technology-based solutions, and responsive governance.</p>
<p class="mb-4">
SARS-CoV-2, the coronavirus that causes COVID-19 is zoonotic, that is, it was transmitted to humans from animals. Many newly emerging or re-emerging viral infections, including Ebola, plague, Zika, SARS and MERS are zoonoses. Agricultural practices, animal husbandry, food production, changes in land use, hunting practices, deforestation and ecosystem breakdown have all been identified as contributing to the rising risk of zoonotic infections.  </p>

<h3>Vulnerable areas</h3>
<p class="lead">
Biological hazards affect people at all levels of society and in all countries because infectious diseases travel easily across borders. New pathogens continue to emerge by mutating, adapting and travelling from one species to another. Biological hazards can be endemic, that is constantly present in a community – they pose low risk when the population is largely immune, but risk becoming epidemics when they are introduced to a new host community with no immunity.</p>
<p class="mb-4">
The HIV/AIDS pandemic, which has claimed more than 32 million lives since it was identified in 1981, shows how biological hazards often exploit the fault lines of society, spreading in the shadows of marginalization, disruption and conflict. Droughts, floods, earthquakes and large displacements of populations also create conditions favourable for disease transmission.

	          </p>

<div class="bg-white p-4">
