            	</div>
			</div>      
    </div>
    <div class="col-md-7 ">
	    <p class="lead mb-4">
		          Wildfires are not a major cause of death, but they can be very destructive. Many wildfires are caused by human activities, either accidentally or as a consequence of carelessness, or arson. These fires often get out of control and spread over vast areas extending to tens or hundreds of thousands of hectares.
</p>

<h3>Risk factors</h3>
<p class="lead mb-4">
The Global Fire Monitoring Centre, which is monitoring worldwide fires, is projecting increasing fire risk in the future due to increasing demand for agricultural lands for food and the necessity to use fire for land-use change, the expansion of residential areas/infrastructures built near fire-prone vegetation, and extended periods of drought. Wildfires will cause more land degradation (soil erosion, loss of land productivity) and as a consequence will create more flooding and landslides.
</p>

<h3>Vulnerable areas</h3>
<p class="lead mb-4">
Agricultural and pasture lands in which fire is used for controlling weeds, bush encroachments, and for land clearing are the most wildfire-prone areas as well as fire-prone natural forest, bush land and grassland ecosystems with high occurrence of natural fires in the subtropics (e.g. Africa, Australia), or in the northern latitudes (e.g. North America, Russia), and agricultural and forest plantations (e.g. eucalypt and pine plantations). </p><p class="lead mb-4">Other vulnerable areas include: residential areas or scattered houses/infrastructures nearest to fire-prone vegetation; residential areas or individual structures made of easily flammable materials (e.g. wood, thatch, wooden shingles or otherwise easily ignitable roofing materials); and abandoned rural villages and human settlements with no one to manage, prevent or respond to wildfires.	          </p>

<div class="bg-white p-4">
<h4>Wildfires: Disaster risk reduction measures</h4>
<p>Limit development in high bushfire risk areas and clear the vegetation surrounding homes and other structures.</p>
<p>Build fire lanes or breaks between homes and any forested or bush land areas, if a natural firebreak (such as a road or a river) does not exist.</p>
<p>Plant vegetation of low flammability.</p>
<p>Use fire-resistant building materials.</p>
<p>Use traditional and advanced methods of prescribed burning for sustainable agriculture and flora and fauna management.</p>
<p>Enact legislation and regulation at the appropriate jurisdictional levels.</p>
<p>Conduct community-based fire risk minimization activities during all stages of fire management.</p>
<p>Provide community alerts through fire danger rating systems; these systems forecast the potential for fire, based on recent rainfall, temperature, wind speed, and fuel on the ground.</p>
<p>Educate the community and raise public awareness about the risks of wildfires as people are often responsible for wildfires.</p>
<p>Develop firefighting capacities and public safety.</p>

