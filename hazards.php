    <div class="col-md-7 ">
	    <p class="lead mb-4">
		         Tornadoes kill fewer than 100 per year on average but they can be very destructive and cause huge economic losses. The United States is a major hotspot with about 1,000 tornadoes every year, causing 80 deaths and more than 1,500 injuries per year. </p><p class="lead mb-4">
Tornadoes are vertical funnels of rapidly spinning air. Their winds may top 400 km/h and can clear-cut a pathway of more than 1 km wide and 80 km long. Most tornadoes are about 100 meters wide; larger tornadoes can be 1 km wide and travel for 50 km or more. Tornado size does not necessarily indicate how destructive it is. Small tornadoes can also be very destructive. Tornadoes have many distinct shapes, sizes, colours and intensities.</p>

<h3>Risk factors</h3>
<p class="lead mb-4">
Lack of early warning systems and preparedness programmes increase the risk for all populations. However, even when warning systems are in place, the aged and children have higher mortality rates given their lower capacity to respond. Populations living in mobile homes are at greater tornado risk. The rate of serious injury for mobile homes occupants is 85.1 per 1,000 compared to 3 per 1,000 for occupants in standard homes. </p>

<h3>Vulnerable areas</h3>
<p class="lead mb-4">
The most tornado-prone areas in the world are in North America, in particular the Great Plains in the United States and south-central Canada. Tornado Alley, a region that includes eastern South Dakota, Nebraska, Kansas, Oklahoma, northern Texas and eastern Colorado, is home to the most powerful and destructive of these storms.
</p><p class="lead mb-4">
The United States gets 75 per cent of all the world’s tornadoes, followed by Canada and Bangladesh. Australia, New Zealand, South Africa, India, Argentina and the Russian Federation are also prone to strong tornadoes.</p><p class="lead mb-4">
Communities living in poorly built houses close to potential flying objects are in particular danger. People outdoors when tornadoes occur are at higher risk of mortality.
	          </p>
