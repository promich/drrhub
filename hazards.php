<p class="lead mb-4">
The most destructive tsunamis are generated by large, shallow earthquakes with an epicentre or fault line near, or on the ocean floor. Usually, it takes an earthquake with a Richter magnitude exceeding 7.5 to produce a destructive tsunami. Receding water is one of the first visual signs of a tsunami. Experts believe that a receding ocean may give people as much as five-minutes warning to evacuate the area.
</p>

<h3>Risk factors</h3>
<p class="lead mb-4">
More people are living by the sea, in earthquake-prone zones, in poor quality buildings, all of which makes more people vulnerable to a tsunami. The destruction of the environment and natural barriers and the lack of coastal land-use planning are factors that aggravate tsunami impacts. The development of tourist settlements in tsunami-prone zones with no tsunami risk assessment also increases vulnerability.</p>

<h3>Vulnerable areas</h3>
<p class="lead mb-4">
All oceanic regions of the world can experience tsunamis, but in the Pacific Ocean and its marginal seas, there is a much more frequent occurrence of large, destructive tsunamis because of the many large earthquakes along the margins of the Pacific Ocean.
	          </p>

<div class="bg-white p-4 mb-4">
<h4>Tsunamis: Disaster risk reduction measures</h4>

<p>Understand what a tsunami is and how it develops and its impact on the coast.</p>
<p>Have a tsunami hazard map, including clear designation of shelter or safer areas, which can be reached immediately.</p>
<p>Have an early warning system in place.</p>
<p>Reinforce building structures: move homes and buildings away from the shoreline.</p>
<p>Protect essential infrastructure such as schools and hospitals, roads, harbours, power plants, banking, and build structures at higher levels if possible.</p>
<p>Install seawalls and revetments, structures that can be built along the shoreline to help protect the shore from storm waves.</p>
<p>Ensure that natural barriers - dunes, mangroves and coral reefs - are protected; they help mitigate impact on shore.</p>
<p>Keeping tsunami indigenous knowledge and practices alive in the memory of vulnerable populations can also save lives.</p>
<p>Educate populations and tourists on tsunami risks and integrate tsunami education in the school curricula.</p>
<p>Make sure people know that when seawaters recede noticeably, everyone must head for high land.</p>
<p>Place tsunami evacuation signs along roadways clearly indicating the route to safety  inland or to higher ground.</p>

