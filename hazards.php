    <div class="col-md-7 ">
	    <p class="lead">
		          Droughts refer to extended periods of below-normal rainfall. They are a weather-related natural hazard, which can affect vast regions for months or years, with a significant impact on economic performance, particularly food production. Millions of people are affected by droughts each year and it is expected that vulnerability to drought will increase due to population increases, environmental degradation, development pressures and climate change.</p> 
<p class="lead mb-4">
There is little, if anything, that can be done to alter the occurrence of droughts. However, it is important that scientists try to understand and communicate the probability of drought events of various levels of intensity and duration.</p>
<p class="lead">
Droughts can be defined in different ways: 
</p>
<ul>
<li class="lead">
Meteorological drought – a measure of departure of precipitation from normal; due to climatic differences what is considered a drought in one location may not be a drought in another location.</li>
<li class="lead">
Agricultural drought refers to a situation when the amount of moisture in the soil no longer meets the needs of a particular crop.</li>
<li class="lead">
Hydrological drought occurs when surface and subsurface water supplies are below normal.</li>
<li class="lead">
Socio-economic drought refers to the situation that occurs when physical water shortage begins to affect people.
</li>
<li class="lead mb-4">
The World Meteorological Organization (WMO) has recently adopted the Standardized Precipitation Index (SPI) as a global standard to measure meteorological droughts on the basis of rainfall data. </li>
</ul>
</p>




<h3>Risk factors</h3>
<p class="lead mb-4">
Drought risks are not only associated with deficient or erratic rainfall but with poverty and rural vulnerability, poor water and soil management, weak or ineffective governance and climate change. Climate change will contribute to a shortage of rainfall and consequently desertification. </p>

<h3>Vulnerable areas</h3>
<p class="lead mb-4">
	Droughts affect all climactic regions, but parts of Africa are among the most vulnerable. For example, in the African Sahel, warmer and drier conditions have led to a reduced growing season with detrimental effects on crops. In southern Africa, longer dry seasons and more uncertain rainfall are prompting adaptation measures. Poor rural households, whose livelihoods depend on rain-fed subsistence agriculture are the social groups most exposed and vulnerable to drought. Droughts are rarely, or solely, responsible for conflicts, but they can contribute to the likelihood of conflict by increasing competition for scarce resources and by exacerbating ethnic tensions usually due to displacement or migration.
	          </p>

<div class="bg-white p-4">
<h4>Droughts: Disaster risk reduction measures</h4>
<p>
Policy and governance, political commitment for drought risk management.
</p>
<p>Drought risk identification, impact assessment and early warning, including hazard monitoring and analysis.</p>
<p> 
Drought awareness and knowledge management to create the basis for a culture of drought risk reduction and resilient communities.</p>
<p>
Effective drought mitigation and preparedness measures.</p>
<p>
Building codes and appropriate materials to reinforce resilience.</p>
<p>
All of these elements need strong political commitment, community participation, and consideration of local realities and indigenous knowledge.</p>
