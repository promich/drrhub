    <div class="col-md-7 ">
	    <p class="lead mb-4">
		          A heatwave is a prolonged period of extremely high temperatures. They vary according to location of a particular region and the time of year and there is no universal way of defining or measuring heat waves. Heatwaves are often accompanied by high humidity and can exacerbate urban air pollution which can affect the elderly, pregnant women and children. </p> <p class="lead mb-4"> 
The impacts of heatwaves can be very catastrophic as we saw in India in May 2015, where  at least 2,500 people died of the consequences of high temperatures reaching over 45 degrees Centigrade.  Europe was also badly affected by high and unusual temperatures in the summer of 2003 leading to health crises in several European countries and killing a total of 70,000 people, mostly older persons. 
</p>


<h3>Vulnerable areas</h3>
<p class="lead mb-4">
Regions that are more susceptible to heat waves include inland deserts, semi deserts and Mediterranean-type climates. </p><p class="lead mb-4">
Cities are particularly vulnerable to heatwaves as urban areas experience higher temperatures during the summer due to buildings, roads, and other infrastructures absorbing solar energy. Heat waves disproportionately impact people who are poor, elderly and young. 
	          </p>

<div class="bg-white p-4">
<h4>Heatwaves: Disaster risk reduction measures</h4>
<p>Early warning systems are the best way to protect people. However, they need to be accompanied by drills and evacuation exercises for community participation and immediate response.</p>
<p>Structural measures such as air conditioning and cooling systems can reduce the negative impact of heatwaves.</p>
<p>Heat wave risk assessment should be integrated into urban planning and health management policies.</p>
<p>Raise community awareness, and prepare communities especially the very young, elderly and disabled. Incorporate education on heatwaves in schools, hospitals, public and private sectors and social activities.</p>
<p>Protect animals as well.</p>

