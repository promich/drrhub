    <div class="col-md-7 ">
	    <p class="lead mb-4">
		    Floods affect more people than any other hazard. Worldwide, nearly 200 million live in coastal zones at risk of flooding. Flooding is usually the result of heavy or continuous rain that exceeds the absorptive capacity of the soil and the flow capacity of rivers, streams and coastal areas. Floods can be triggered by thunderstorms, tornadoes, tropical cyclones, monsoons, melting snow and dam breaks. The most common floods are flash floods, snowmelt floods, coastal floods and river floods. Flash floods and sudden floods are the most dangerous, especially when they occur at night. </p>

<h3>Risk factors</h3>
<p class="lead mb-4">
Rapid population growth, rapid urbanization, environmental degradation such as the loss of forests and natural flood buffers, and climate change, will expose more people to future floods. Melting glaciers and rising sea levels will bring floods to places not previously at risk. </p>

<h3>Vulnerable areas</h3>
<p class="lead mb-4">
Developing countries are most at risk and although Asia remains the continent most hit by floods, Africa and Latin American countries are also heavily affected. </p><p class="lead mb-4">
The poor, with the least means to adapt are often forced to live in high-risk places: slopes, flood plains, ravines, or in crowded, urban low-lying areas in mega-cities.
	          </p>

<div class="bg-white p-4">
<h4>Floods: Disaster risk reduction measures</h4>
<p>Integrate flood risk assessment into urban planning strategies; avoid building on flood-prone land, develop new building codes to reinforce flood resistance, and create more space for rivers, flood plains and wetlands.</p>
<p>Ensure health of costal reefs and mangrove plantations, which can reduce the speed of seawater, wave strength and wind force in coastal storm surges.</p>
<p>Maintain early warning systems, backed up by regular drills and evacuation exercises.</p>
<p>Have an evacuation plan for those at risk, including the elderly, disabled and very young.</p>
<p>Catalyse finance and insurance schemes to protect assets and livelihoods, which are often destroyed by floods.</p>
<p>Protect and evacuate animals.
