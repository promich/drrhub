<p>
Now I’d like to sample some CAP-enabled alerting systems.
</p><p>
This diagram gives a sense of the range of alerting methods that a CAP-based alerting system might support when it is deployed.
</p><p>
Notice that television, radio, and even devices like modern sirens can take up CAP messages.
</p><p>
I am going to sample just a few CAP systems I know about personally. This is a tiny proportion of CAP systems in operation.
</p><p>
Here we see how CAP is becoming part of the international Flash Flood Guidance System, developed by the Hydrologic Research Center.
</p><p>
Next, I want to show how CAP is implemented in my own country: the United States.
</p><p>
Here you see several United States alerting authorities listed in the Register of Alerting Authorities, with links to their CAP news feeds.
</p><p>
Two are operated by the National Oceanic and Atmospheric Administration (NOAA): the National Weather Service CAP news feed and the National Tsunami Warning Center CAP news feed.
</p><p>
A recent addition from the United States Geological Survey (USGS) is the CAP news feed for the Volcano Hazard Program.
</p><p>
The Environmental Protection Agency has a CAP news feed for Air Quality Alerts. EPA is extending this approach to many cities worldwide through its AirNow initiative.
</p><p>
The Integrated Public Alert and Warning System (IPAWS) helps authorized officials to deliver alerts to the U.S. public through multiple communications pathways. You may notice that IPAWS lists broadcast radio and television first in its distribution media.
</p><p>
IPAWS aggregates CAP alerts from more than 1,500 federal, state, local, tribal and territorial alerting authorities. With so many CAP sources available, providing a consolidated CAP news feed at national scale is essential to broadcasters throughout the United States. IPAWS is also the gateway to the national cell broadcast system for emergency alerting.
</p><p>
As I will explain in the CAP Alert Hub section, the same situation operates at international and global scales. Aggregation of CAP news feeds allows large-scale broadcasters to get alerts from just a few consolidated sources.
</p><p>
Yet, the individual CAP news feeds remain available and are used by more local broadcasters who service towns, cities, small islands, etc.
</p><p>
Here we see a similar situation in Europe.
</p><p>
The MeteoAlarm system is operated on behalf of 37 European national weather services. MeteoAlarm features a graphic Web page intended to highlight severe weather situations. But, MeteoAlarm also publishes a CAP news feed specific to each partner country.
</p><p>
Again, this aggregation of CAP news feeds allows for national-scale needs to be served in addition to European-scale needs. And, these 37 MeteoAlarm feeds are aggregated by global-scale CAP Alert Hubs.
</p><p>
This is a diagram of India’s CAP-enabled "Integrated Disaster Early Warning Platform".
</p><p>
This system is in use by disaster management authorities. In 2020, it has been used extensively for disseminating alert messages related to the COVID-19 pandemic.
</p><p>
All Mobile Operators of India have been integrated with the platform and most of those support cell broadcast. Broadcasters can note that all-India Radio is on board and dissemination through television is at an advanced stage.
</p><p>
In addition to English, support for Hindi, Tamil, and Malayalam allows 65% of the population to get alerts in their own regional language.
</p><p>
The aggregation across scales is illustrated here as a vertical dimension. It ranges from the village and city level, up through the District and State levels, and on up to the National level.
</p><p>
India's national-level CAP news feed is already aggregated by CAP Alert Hubs at global scale. Regional-scale CAP Alert Hubs that would include India are in the works as well.
</p><p>
Let me also highlight China.
</p><p>
China's CAP-enabled alerting for all hazards nationwide is called National Early Warning Release System (NEWRS). NEWRS transforms warnings from different agencies and releases them to the public throughout China over multiple media: TV and radio, mobile, internet, and locally-designed methods, among others..
</p><p>
NEWRS is one of the world's most extensive CAP-enabled warning systems. It is comprised of one national, 31 provincial, 343 municipal, and 2,015 county centers.
</p><p>
NEWRS  is working on automatic conversion of a published alert to a 15-second vlog ("video blog") within one minute. That video will then be made available to relevant accounts throughout the alerting area.
</p><p>
China's CAP news feed is aggregated into global-scale CAP Alert Hubs.
</p><p>
It is also part of an Asia-wide CAP aggregation known as the Global Multi-Hazard Alerting System - Asia (GMAS-A).
</p><p>
Let me conclude this sampling with a global view of CAP uptake at the country level.
</p><p>
Today, 68% of the world's population lives in a country with at least one national CAP news feed operating. Those show as green on this map.
</p><p>
The countries shown in gray have not even begun to implement CAP, as far as we know. A large percentage of these are "Least Developed" or "Developing" countries. Sadly, those are also countries where many people are especially vulnerable to disasters.
</p><p>
Yet, it is easy for such countries to implement CAP, and at no cost.
</p><p>
There are several free tools for CAP alert creation and publishing. Here is the link for a tool called "CAP Editor".
</p><p>
This free tool is already initialized for about 350 alerting authorities across 200 countries and 18 languages, and more can be added easily. As a cloud-based service, this is a very easy option for alerting authorities to implement CAP.
</p><p>
Anyone can try out the CAP Editor tool as a guest. To actually publish alerts, it is only necessary that specific persons be authorized by the alerting authority to compose or approve its CAP alerts.

</p>
