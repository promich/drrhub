		          <h3 class="mb-4">Recognising the power of media narratives</h3>
            	</div> 
			</div>      
            
    </div>
<div class="col-md-9 col-lg-5">
	<p class="lead mb-4">
Media coverage is often selective, not comprehensive, and it creates narratives that gather momentum to the extent that they determine how disasters and various actors in the response to disasters are viewed in the public eye. Media narratives also determine where blame is laid, and who is blamed for failures or appreciated for an effective disaster response. </p>
<p>
Hurricane Katrina, which hit the city of New Orleans in the USA in 2005, and the 9/11 attacks in 2001 represent the power of public perception that arises from media coverage. While in the former case, the meta-narrative that emerged was about the failure of the government, the media narrative in the latter case created a supportive consensus between the people, the media and the government. Similar examples of dominant narratives abound in the various national responses to the COVID-19 pandemic in 2020.</p>
<p>
Media narratives gather momentum because the media is often ‘self-referential’, that is, journalists can be guided by the angles covered by other journalists even as they compete with each other. Simply put, once a topic or perspective gains attention, it attracts more attention – and because it attracts more attention, it becomes newsworthy and creates a news wave. It is important to recognise this and make attempts to break this loop where appropriate. </p>


</div>

