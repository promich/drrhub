<div class="col-md-9 col-lg-5">
	<p class="lead mb-4">
There is an intense desire to know what is happening and how that will affect people’s lives during disasters, but the public’s right to know can come into conflict with the wider interest of those very groups. One such example can be seen in the leak of information about a quarantine plan in northern Italy, which was revealed by a newspaper the day before it was to come into effect, thus nullifying it and creating far more widespread problems for Italy as a whole. Leaks and disclosures on such sensitive matters deserve thorough editorial consideration at the highest levels of the media, and the journalistic imperative to be first with the story should not be the only factor taken into account.</p>





