                          <h4 class="h3 mb-3">The ethics of aiding those in distress</h4>
                          <p>Reporting on war, natural disaster, and epidemics – and alerting the world to human suffering and need – serves a compelling public interest. On such assignments, journalists inevitably encounter people with urgent needs for shelter, food, or medical attention. Human decency prompts many journalists to offer aid and comfort to people who are suffering, but reporters must not profit from these acts nor exploit those whom they help.
</p><p>
People in distress who receive aid from a journalist may feel obligated to help that journalist in his or her job. Even under difficult reporting conditions, journalists must ensure that subjects of news reports and photos, audio, and video recordings give consent freely. If journalists have given aid, they should seek other faces for their stories.  Journalists who also are doctors, nurses or other health care professionals should consider as well the Hippocratic Oath and other professional standards that require caregivers to "keep secret" what they glean from patients.
</p><p>
Giving aid to people in need is natural and often commendable, but in a media environment where celebrity brings financial rewards, stories that feature journalists' aid efforts elevate their personal interests and those of their employers above the public's interest.
</p><p>
In summary, do not exploit vulnerability for gain or glory.
</p>
