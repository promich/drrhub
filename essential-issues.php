This is one of the big issues in the coverage of disasters, especially when they are happening and in their immediate aftermath.</p>
<p class="lead mb-4">
In some cases, journalists may find themselves first on the scene, even before the arrival of emergency services. Though their primary job is to get the information out from that location to the wider world, they might be confronted by distressed people whose real needs are medical care, reassurance, food, water and shelter. This is a real dilemma that many disaster journalists face. </p>
