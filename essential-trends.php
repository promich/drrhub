	<h4>Trend</h4>
	<p class="lead">As cities continue to grow and as more people crowd into poorly built housing settlements, the trend of earthquake-related deaths will probably continue to rise. It is expected that the number of people exposed to cyclone and earthquake risks in cities in developing countries will double by 2050. 
</p><small>(World Bank Global Urban Risk Index, 2013)</small>
