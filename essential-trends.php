<div class="col-lg-7">

<h4 class="">Disasters affect more than 200 million people per year </h4>
<p class="lead mb-4">
The numbers affected by disasters can vary from year to year – large-scale disasters can affect the health, livelihoods and habitats and tens of millions over a short period and tolls obviously spike when those happen. The COVID-19 pandemic, for instance, will have affected the vast majority of the world’s population in some way or the other before it comes to an end, and will cause immense economic damage on a scale seldom seen before.  
</p>
<p>
Between 2000 and 2019, more than 4 billion people were affected by disasters – an average of more than 201 million every year and some more than once. Again, there are variations between continents. In Asia, more than 1.5 billion people were affected by floods over these 20 years, and 1.12 billion by drought. In Africa, the biggest disruptions were caused by drought (244.8 million) and floods (54.7 million).
</p>


