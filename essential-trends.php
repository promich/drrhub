<div class="col-lg-7">

<h4 class="">Women, children, aged and disabled people are among the most vulnerable</h4>
<p class="lead mb-4">
Women and children are 14 times more likely to die than men during a disaster, according to the International Union for Conservation of Nature (IUCN). In industrialized countries, more women than men died during the 2003 European heatwave, and many more African-American women were affected by Hurricane Katrina in 2005 than men. In many countries, women have subordinate positions, restricted mobility, less educational opportunity, less voice in decision-making and poorer employment, all of which increases vulnerability.</p>
<p>
Older women were among the most affected during the 2011 Great East Japan earthquake. Among the 13,007 fatalities in Iwate, Miyagi, Fukushima -- the country's three most affected prefectures; 56.1% were aged 65 or over, and most were women according to the Japanese National Police Agency (NPA). 
</p>

</div>
<div class="col-lg-5 offset-xl-1 col-xl-4">
	<div class="bg-primary-2-alt p-4 mb-4">
	<h4>Trend</h4>
	<p class="lead">There has been some progress with women and children and disabled people vis-a-vis awareness raising and preparedness. But as long as these groups continue to be largely excluded from disaster risk reduction decision-making and education, no real progress will be achieved.</p>
	</div>
	
	
</div>
</div>



<div class="row mt-6">
<div class="col-lg-7">

<h4 class="">Un-reported small-scale disasters create long-term impacts</h4>
<p class="lead mb-4">
Tens of thousands of small-scale disasters occur each year throughout the world because of flooding, landslides, fires and storms, but are not necessarily reported or recorded in international databases.</p>
<p>The impact of small disasters can be just as damaging as large ones, and even more so for poor people, causing injury and death, undermining livelihoods and leading to chronic poverty. Low-intensity events, such as recurrent floods rarely attract media interest and are poorly documented, but they can have significant cumulative impacts.
</p>

</div>
<div class="col-lg-5 offset-xl-1 col-xl-4">
	<div class="bg-primary-2-alt p-4 mb-4">
	<h4>Trend</h4>
	<p class="lead">Small-scale disasters are often unreported but have an increasingly huge impact on development and poverty.</p>
	</div>
	
	
</div>
</div>


<div class="row mt-6">
<div class="col-lg-7">

<h4 class="">For every $100 spent on development aid, just 40 cents are invested in preventing disasters </h4>
<p class="lead mb-4">
Disaster Risk Reduction has been at best a low priority. A study by Jan Kellett and Alice Carivani shows that in the period 1991–2010, the total quantum of aid provided by the international community was more than $3 trillion. Of this, $106.7 billion was allocated to disasters, and within that, only $13.5 billion was for risk reduction measures undertaken before disasters strike. Larger sums were allocated to other aspects – $23.3 billion was spent on reconstruction and rehabilitation and $69.9 billion on response.</p> 

<p>
Of overall aid financing over those 20 years, the $13.5 billion spent on DRR accounts for just 0.4% of the total quantum of international aid. Essentially, for every $100 spent on development aid, just 40 cents were invested in defending that aid from the impact of disasters (Source: Financing Disaster Risk Reduction, Jan Kellett and Alice Carivani, 2013). According to UN figures, in 2014, just 0.4% of the global aid budget of $135.2bn roughly $540m was spent on reducing disaster risk.
</p>

</div>
<div class="col-lg-5 offset-xl-1 col-xl-4">
	<div class="bg-primary-2-alt p-4 mb-4">
	<h4>Trend</h4>
	<p class="lead">Disaster risk reduction is often seen as a long-term solution, whereas in reality it serves another purpose as a short-term solution with immediate returns that can considerably reduce poverty, climate change impacts and disaster risks. DRR is not about asking for more money but for using development and humanitarian aid money in different way.</p>
