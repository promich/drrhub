	<p class="lead">
	Below is a list of 10 disaster-related patterns and trends that you should know before reporting on a disaster risk reduction story.</p><p> Please note that most of the figures given below have been averaged out over several years as mentioned below, and these average figures are affected by large-scale disasters such as the Indian Ocean Tsunami of 2004 or the Haiti earthquake of 2010. Each of these disasters killed hundreds of thousands of people, which affects annual and decadal averages. </p>

