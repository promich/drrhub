<div class="row mt-6">
<div class="col-lg-7">

<h4 class="">Earthquakes, storms, epidemics and floods claim the most lives</h4>
<p class="lead mb-4">
More than 1.5 million people died because of disasters between 2000 and 2019, yielding an average of 75,241 deaths in each of these 20 years.
</p>
<p>
Earthquakes were the biggest killers, claiming 721,315 lives, almost half of the total 2000–19 death toll, followed by storms (199,712), epidemics (110,580) and floods (104,609). Big disasters can take a heavy toll. The Indian Ocean tsunami of 2004 and the Haiti earthquake of 2010 each claimed more than 200,000 lives. The death toll of the COVID-19 pandemic rose rapidly, dwarfing these figures and making it the deadliest disaster so far this century.
</p>
<p> 
Different continents face different conditions, depending on which types of disasters are most prevalent. In Asia, the highest death toll was due to earthquakes (489,901 from 2000-2019), whereas in Africa, epidemics were the biggest killers, taking 86,862 lives. 
</p>

<figure class="figure">
    <img src="assets/img/fig4-annual.jpg" class="figure-img img-fluid" alt="UNCCC">
