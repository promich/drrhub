	<h4>Trend</h4>
	<p class="lead">As more poor people concentrate in urban slums, the numbers of people vulnerable to disasters will also increase. Three billion of the world’s people live in poverty on less than US$2 per day and 1.3 billion on less than US$1 per day. According to UN-Habitat, by 2030, nearly 3 billion people will live in slums.</p>
	</div>
	
	
