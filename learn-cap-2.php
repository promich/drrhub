	  <p>
Our first topic is "Getting the Key Facts".
	  </p><p>
Consider this Breaking News: A Flood is Coming.
	  </p><p>
Lives are at stake. People need to be warned.
	  </p><p>
You are a broadcaster. You can help warn people in harm's way.
	  </p><p>
But first, you need the Key Facts for this emergency.
	  </p><p>
Here are the some Key Facts people need to know:
	  </p>
<ul>
<li>
What is the emergency?
</li><li>
Where is the affected area?
</li><li>
How soon do we need to act?
</li><li>
How bad will it be?
</li><li>
How certain are the experts?
</li><li>
What should we do?
</li>
</ul>
<p>
CAP is the international standard format for carrying those Key Facts.
	  </p><p>
CAP is designed for all types of hazards, and we'll see that CAP is useful across all kinds of media.
	  </p><p>
CAP is tightly focused on enhancing communication in the context of emergencies. It is especially relevant to the Preparedness and Response phases of the Emergency Management Cycle.
	  </p><p>
In terms of Disaster Risk Reduction, use of CAP aligns with Target (g) of the Sendai Framework: to "substantially increase the availability of and access to multi-hazard early warning systems".
	  </p><p>
Looking at this diagram, we picture CAP as a "standard business form" that you might  have in paper on a clipboard. The basic information on that form is relevant to just about everyone involved in the emergency.
	  </p><p>
Of course, a CAP alert by itself doesn't have everything about the emergency. CAP just carries some key facts--facts that everyone needs.
	  </p><p>
In a complex, extensive, or evolving emergency, experience shows that multiple trusted authorities will be involved. Those authorities often have separate and distinct responsibilities.
	  </p><p>
Scientific and technical agencies are typically expert at characterizing a hazard threat and providing guidance on its expected impacts. But, such agencies are usually not empowered to instruct people on specific actions such as evacuation. Their CAP alert might simply instruct people to "Monitor local media for instructions from your civic authorities".
	  </p><p>
Civic Authorities do have the power to instruct people on actions such as evacuation. A CAP alert from the civic authority can quote the hazard threat description by the scientific and technical agency. It would embellish that with instructions such as planned evacuation routes.
	  </p><p>
When an emergency is complex or extensive, it is also common to have overlapping jurisdictions, and different authorities can engage as the emergency evolves. A flood that first affects a single town can later affect a large city, and health alerts can be needed from secondary effects.
	  </p><p>
Use of CAP by all alerting authorities  to communicate Key Facts as an emergency unfolds helps ensure that those facts are communicated coherently. This applies to private communications among themselves, as well as the messaging from authorities to the public.
	  </p><p>
Because most emergencies are small-scale and occur often, people get familiar with their local and routine alerting systems. In many places, the alerting systems for severe weather are used as a backbone system for many other kind of hazard threats as well.
	  </p><p>
In contrast, disasters are always larger scale and much less frequent.
	  </p><p>
Where alerting authorities are using CAP, it is common to see routine emergency alerting designed to scale up to handle disaster early warning. People then get alerted to a disaster through the same alerting system they already know and trust.
		  
		  
		  
