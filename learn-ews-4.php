<p>
Our next topic: 'Many Roles Pulling Together'.
</p><p>
Consider the many agencies and institutions that might play a crucial role in a disaster. Here are a few examples:
</p>
<ul>	
<li>Governments and International Institutions
</li><li>Non-Governmental Organizations (NGOs)
</li><li>Humanitarian Aid Agencies
</li><li>Volunteer Organizations
</li>
</ul>
<p>
We look first at the government sector. Depending on the scale of the emergency, multiple levels of government are likely to be involved in the emergency management functions, in a monitoring role if not operationally.
</p><p>
In the event of a disaster situation at national scale or crossing the border with a neighboring country, relevant actors could include the Head of the Government.
</p><p>
Other front-line government agencies for a national-scale disaster include:
</p>
<ul>	
<li>Ministry of Foreign Affairs, for aspects related to international cooperation and coordination
</li><li>National Disaster Management Organization, or other agency responsible for coordinating the government response to disasters
</li><li>National Telecommunications Regulatory Authority
</li><li>Scientific or technical agencies with expertise in the particular natural or technological hazard threat
</li><li>Responder Agencies: police, firefighters, civil protection, health, and so on.
</li>
</ul>
<p>
Many national institutions with a key role in emergency management have international counterparts, and here are a few examples.
</p>
<ul><li>A nation might have an Emergency Management Agency, Disaster Management Office, or Civil Protection Agency. The international counterpart is the UN Disaster Risk Reduction Office (UNDRR).
</li><li>A National Telecommunications Regulatory Agency counterpart on the international scale is the International Telecommunication Union (ITU).
</li><li>A National Meteorological and Hydrological Service has its counterpart in the World Meteorological Organization (WMO).
</li><li>And, a national Ministry of Health has its counterpart in the World Health Organization (WHO).
</ul>
<p>
These international institutions can be very helpful to a country, especially with  regard to trans-border issues and coordination opportunities.
</p><p>
Broadcasters and other media play a crucial role in disseminating relevant information before, during and after disasters. These providers should include wireless and fixed service providers, as well as satellite providers, public safety radio networks, TV and radio broadcasters, and Internet service providers, among others.
</p><p>
Television and radio broadcasting is among the most powerful means of transmitting information to the general public. Broadcasting has been in the public service for a very long time, with radio broadcasting dating back to the early twentieth century, and television broadcasting in service since 1930. Although online services are growing rapidly, broadcasting services continue to reach a high percentage of the public.
</p><p>
In case of emergency, early warning systems can be connected to broadcasting stations in such a way that they interrupt the usual programming to transmit information to the public, such as evacuation orders.
</p><p>
Government entities dealing with emergencies should be in communication with television and radio broadcasters when the situation warrants. Government should also facilitate journalists as they cover events in real time. If access to the affected areas is too risky, meeting points for journalists should be set up.
</p><p>
Emergency information can be disseminated through private networks as well as any government-operated networks.
</p><p>
Often the owners and operators of a country's critical infrastructure, private networks such as Mobile Radio Services (cellular telephones) may be key to ensuring the resilience and reliability of communications, before the disaster as well as during emergency response and recovery.
</p><p>
Non-Governmental Organizations (NGOs) can play vital roles during emergency preparedness, response and recovery operations.
</p><p>
In the Early Warning context, they often contribute valuable resources that help disseminate relevant information about the disaster, and they may provide backup communications when other forms of communications have failed or have been disrupted.
</p><p>
Volunteer emergency communications operators, and groups using amateur radio, can help disseminate relevant information about the disaster and may provide backup communications when other forms of communications have failed or have been disrupted.
</p><p>
Coordination among the many stakeholders involved in a disaster is always a challenge. Nations each have their own ways to achieve such coordination, but guidance on effective approaches is available through international institutions as well.
</p><p>
International institutions themselves confront major coordination challenges when a disaster strikes. An important institution in that regard is the Inter-Agency Standing Committee (IASC).
</p><p>
IASC was created by the UN General Assembly in 1991. It is the longest-standing and highest-level humanitarian coordination forum of the UN system. IASC brings together the executive heads of eighteen UN and non-UN organizations to ensure coherence of preparedness and response efforts.
	  
</p> 	  
	  
	  
