	  <p>
In this block we've looked at ways we can expand our programming outside the news corridor.</p><p>
It's possibly stimulated some of you to think of DRR in another light but this isn't just a "talking shop" where we suggest ideas. We need to take this knowledge and information on to the next level. Dealing with disaster risk reduction can't be a "sometimes maybe" situation. The hazards are there, they are increasing, more disasters will happen. What we've tied to show throughout these online blocks is that even though we're dealing with complex issues, our contribution as broadcasters is essential and we can and need to do more.
Now that the knowledge base is there, we can progress by planning our interventions and programming. This is not a sprint, it's more like a marathon. Our suggestion is that a 12 month target of content is put in place.
Let's start with News.</p><p>
A good premise would be to include a DRR related story on a weekly basis. That is, target 4 stories per month. Perhaps a weekly slot might work?</p><p>
Current Affairs isn't an easy slot to plan but again by considering anniversaries of events and basing it on an investigative footing, at least 2 or 3 programmes could be planned over a 12 month period.</p><p>
A plan for a documentary programme can of course be timeless. Given the right subject matter,- finding the right person to follow maybe  (?) -  the planning of a relevant  and interesting programme is more than possible.</p><p>
Chat show interviews are easier to plan and planning school or youth competitions is not difficult to arrange.
So that's our request at the end of this online series. As programme directors, editors, producers, reporters and researchers.... think about a 12 month plan for your station or organisation.</p><p>
Our next step is to get involved with you on an interactive basis. If we can't travel to be with you, we'll use the technological devices that we have, to take that next step. Hopefully, either with myself or some of my colleagues, we'll get to meet digitally soon. Creating that "Culture of Prevention" is something we can achieve by working together.</p> 	  
	  
