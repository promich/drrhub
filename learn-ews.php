<p>
This module  provides an overview for broadcasters on the role of information and communication technologies, and national strategies, for early warning delivery.
</p><p>
You heard in the prior module that a country's disaster vulnerability depends on its particular hazard threats. Here you will get a sense of the broad range of hazard threats worldwide.
</p><p>
You will learn the four Phases of the Disaster Management Cycle: Mitigation, Preparedness, Response, and Recovery.  You will see that  Early Warning often involves various roles working together, including governments, Non-Governmental Organizations, and broadcasters. As a broadcaster, you also need to be aware of national law, regulation and plans, including any disaster risk management plans.
</p><p>
You may already know  that people in harm's way can find emergency alert messages confusing or not compelling.  The focus needs to be: reaching the audience in terms they understand.
</p><p>
Early warning systems today are using ever more ways to communicate. You will be introduced to the international standard Common Alerting Protocol (CAP). CAP is also the topic of the next module in this series for broadcasters.  Please note: we make no distinction between the words "alerting" and "warning".
</p><p>
Recommendations concerning capacity building, training, and drills will be presented. The module also lays out some recommendations on how Early Warning Systems can better address people with specific needs. 
</p><p>
These are the major topics covered in this module.
</p><p>
The first topic is "Types of Disaster by Hazard Threat".
</p><p>
As a broadcaster, you know that many hazard threats can lead to a disaster. 
</p><p>
The Centre for Research on the Epidemiology of Disasters maintains a database of  disasters. Over seven thousand disaster events are recorded from 2000 to 2019. More than 1.2 million lives lost. Economic losses at almost 3 trillion US dollars.
</p><p>
Floods and storms were the most prevalent events. This was due in large part to a rise in climate-related disasters. Among the natural hazards, earthquakes and tsunami killed the most people. In 2020, the COVID-19 epidemic became the deadliest disaster in this century.
</p><p>
Disaster is defined by the UN Office for Disaster Risk Reduction (UNDRR) as "a serious disruption of the functioning of a community or a society at any scale". A disaster could affect a single community, or it could be large-scale and require a national or international response.
</p><p>
Multi-hazard conditions often exist. When hazards cascade, they can lead  to a large-scale disaster. For example, an underwater earthquake leads to a tsunami, which leads to an industrial accident. That is what happened during Japan's Fukushima nuclear disaster in 2011. We can cite other examples:  droughts lead to famines, storms lead to floods, heat waves lead to wildfires, earthquakes lead to landslides, floods lead to dam failures, and so on.
</p><p>
From the DRR Introduction, recall: Disaster Risk can be viewed as a function of multiple factors, including hazard, exposure, vulnerability and capacity.
</p><p>
Here we are looking at the variety of hazard threats, and how the vulnerability varies across countries, depending on their proximity to particular hazard threats.
</p><p>
A few examples will illustrate this point. Earthquakes and volcanoes occur predominantly in countries near a boundary between Earth's tectonic plates (sometimes labeled "Ring of Fire"). Tsunami mostly affect countries near oceans.  Dust storms are most common in countries near deserts. The likelihood of a hurricane/typhoon is higher near the tropics. Landslides are expected in places with steep hillsides.  And so on.
</p><p>
Any broadcaster should know what kinds of hazard threats are likely in the particular places where the audience is located.
</p><p>
Warning people about the potential impact of a typhoon takes more explaining if your audience almost never experiences typhoons. You have the same challenge in warning for any rare hazard threat,  such as a major tsunami, the sudden eruption of a usually dormant volcano, or a major asteroid striking pretty much anywhere on the Earth.
</p><p>
Here, we classify disasters according to the underlying hazard type. Our classification follows the categories defined by the Center for Research on the Epidemiology of Disasters.
</p><p>
Hazards and their associated disasters are classified as either natural or technological. (The term "man-made" is equivalent to "technological".)
</p><p>
Disasters from natural hazards are divided into five sub-groups: geophysical, hydrological, meteorological, climatological, or biological
</p><p>
Technological disasters are divided into three sub-groups: industrial, transport and miscellaneous.
	  
</p> 	  
	  
	  
