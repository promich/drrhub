<p> 
We’ve seen that DRR addresses a simple yet deadly equation: hazard plus vulnerability plus exposure equals disaster.
</p><p> 
DRR is everything you do to reduce the chance of a hazardous event resulting in calamity.
</p><p> 
All this matters for millions of people across the globe. Here are five essential concepts that illustrate why.
</p><p> 
First, nothing undermines development like disasters.
</p><p> 
This graph shows the total number of disasters of various types between 1975 and 2019. Notice the upward trend: more extreme weather events and natural hazards lead to more disasters.
</p><p> 
Yet most governments haven’t moved away from the “band aid” approach. This guarantees a vicious cycle: disaster, respond, rebuild, repeat...
</p><p> 
The result is that a lot of resources go to picking up the pieces after each disaster, crippling development every time.
</p><p> 
Second, risks are interconnected and systemic.
</p><p> 
As global systems become more complex, hazards can trigger hazards in a cascading effect.
</p><p> 
Think of all the things that are so interwoven: the international financial system, communications and IT, trade and supply chains... 
</p><p> 
There’s even a term for one example of the cascade effect: NATECH: natural hazards triggering technological disasters. Remember how Japan’s 2011 earthquake and tsunami triggered the Fukushima nuclear crisis? That’s an example of that.
</p><p> 
Now, imagine looking down at the peaks of a mountain range poking through a sea of clouds. Each peak represents a realisation of risk: failing infrastructure, financial crisis, food insecurity, species extinction...
</p><p> 
From above, these outcomes look like islands, separate and discrete. But peer beneath the clouds to see the broader landscape, and they appear much more interconnected.
</p><p> 
The wider context is the same: greater exposure and vulnerability to hazards.
</p><p> 
And the underlying drivers of risk are there to see: a hyper-connected financial system, rising poverty, environmental degradation, poorly managed urban development...
</p><p> 
The era of hazard-by-hazard DRR is over. When dealing with risk, we need to consider its systemic nature.
</p><p> 
Third, prevention pays - and inaction costs dearly.
</p><p> 
In 2010, a powerful 8.8-magnitude earthquake hit Chile. The death rate was one person out of every 595 affected.
</p><p> 
That same year, an earthquake 500 times less powerful hit Haiti. One in every 15 affected died.
</p><p> 
The difference in mortality had a lot to do with building codes and other DRR-governance measures. It’s a no-brainer: prevention pays.
</p><p> 
Fourth, climate change is a risk amplifier.
</p><p> 
With climate change, expect to see more frequent and bigger disasters, along with a collapse of ecosystems and migration on an unprecedented scale.
</p><p> 
Yet plans for DRR and climate change adaptation have only just started to be coordinated. 
</p><p> 
The World Health Organisation estimates that 250,000 additional deaths could occur each year between 2030 and 2050 due to climate change.
</p><p> 
Fifth, risk is everyone’s business.
</p><p> 
Who’s responsible for DRR? National and state governments, primarily. But that’s not the whole story. 
</p><p> 
Truth is, managing risk is everybody’s problem. Though we might also see it as an opportunity - an opportunity to transform.
</p><p> 
When we face risks, we can either stick our heads in the sand or see how we can do things differently.
</p><p> 
More needs to be done to empower local authorities and communities by giving them resources and responsibilities for decision-making.
</p><p> 
Meanwhile, journalists can play a role in building resilience at all levels, from the local to the national.	  
</p>	  
	  
