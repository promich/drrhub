	         <h3 class="mt-4">Key concepts</h3>
              <p class="lead">Disasters are not “natural”. Yes, there are “natural hazards” such as earthquakes and hurricanes. But there are no “natural disasters”, for these hazards need not lead to a disaster.</p> 
<p class="lead mb-4">Disasters only happen when vulnerability meets exposure to hazards.</p>



