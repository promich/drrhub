<p> 
We all know when a disaster is about to happen or is happening - our news programmes are full of the event... but we shouldn't be limited to that.
</p><p>
Media coverage is a powerful tool that can focus attention - it can set the agenda for what the public are talking about, at any given time, it can influence political decisions, change attitudes attitudes and can save lives.
</p><p>
We in the media are of course a vital channel of information - we stand in between the authorities and emergency service and our audiences - and we pass information in both directions.
</p><p>
Just before a disaster occurs, we're pretty good at conveying warnings and other emergency information. During the event we're normally pretty good at describing what's happening - painting the picture is helpful to everybody involved. So taking social media into account, people look to a credible broadcaster for an authentic picture because misinformation and fake news is a serious problem. We'll talk about ethics at a later stage.
</p><p>
But the premise here is that Broadcasters can do so much more - we're good at reacting but could we be more proactive and shoulder more responsibilities on behalf of our audiences?
</p><p>
Could we take more of a "Watchdog Role" for example? Asking questions of those authorities or organisations who have failed to plan or implement safety procedures or who have been ineffective in their response. It's sometimes seen as "negative reporting" so it has to be performed with high standards of accuracy so that we don't lose credibility. 
</p><p>
Get our facts right and ask the relevant questions.You can save lives by drawing attention to promises that haven't been fulfilled and you can promote resilience over the long term in communities by questioning on their behalf. This pre-disaster period is our weakness - we simply have to improve this area.When we focus on risks and issues, we amplify them and increase the chance of them being addressed.
</p><p>
The International media are good to draw global attention to disaster areas but it's National level media that are best placed to examine policies and funding and to hold the government to account. And it's local media that can get into the final mile to inform communities, that can highlight local risks and can tap into local knowledge.
</p><p>
That's what building resilience in a community is all about. Different roles but equally important and we can learn from the work of other organisations, identify our weaknesses and build our skillset.
</p><p>
So begin with "People" - it's their stories that count because people are always interested in other people - that's how to convince your editors and programme decision makers.
</p><p>
By being more proactive we can  contribute towards efforts that can be made either to prevent such a disaster ever happening again or – if that’s not feasible – mitigating the effects, should it strike again in future. News organizations should feel the need to keep returning to stories to make sure that promised new regulations have indeed been put into place, that overseas aid has gone to those who most need it, that reconstruction is proceeding at a reasonable pace. 
</p><p>
Holding people accountable for their areas of responsibility is one of the basic purposes of a properly functioning media. 
</p><p>
 The media can and should take a prominent role in bringing DRR to public attention, support the relief effort by the rapid dissemination of information, explain the background and causes, and hold people to account for their actions. These are responsibilities that most news organizations would recognize and hopefully, readily accept.</p>
 
