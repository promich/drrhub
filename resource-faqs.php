	<p class="lead">
Disasters caused by natural hazards such as earthquakes, drought, volcanic eruptions, floods, tsunamis and hurricanes can have major political consequences. They often create grievances that lead to conflict by causing mass disruption to people’s lives and livelihoods. In the immediate aftermath of a major disaster, a country’s physical infrastructure is affected, preventing the adequate distribution of food and medical supplies; crops are destroyed, giving rise to food shortages; and localised conflicts can take place over resources. Disasters can also destroy key social and political institutions, thus threatening political stability.</p>
