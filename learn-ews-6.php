<p>
The next topic is "Capacity Building, Training, and Drills".
</p><p>
Preparing for management of an emergency requires continuous training and capacity-building efforts for both those leading emergency responses and the wider community.
</p><p>
Capacity building is key to improving the speed, quality and effectiveness of emergency preparedness and response.
</p><p>
Also, effective training and exercise programs can bolster emergency responders' proficiency with communications equipment.
</p><p>
It can also improve their ability to execute policies, plans and procedures governing the use of communications.
</p><p>
Training exercises and practical activities can help ensure that mobile radio systems and other communications equipment are available to emergency services during an emergency response.
</p><p>
And finally, inclusive preparedness exercises can increase the familiarity of all stakeholders with the specific roles of others involved in emergency response at the sectoral, organizational, and individual level. Within an organization, training and drills can help develop an understanding of the mandate and the working modalities of others involved in emergency operations, in particular for those in charge of communications.
</p><p>
Here are some recommendations for emergency management capacity building, training and drills:
</p>
<ul>
<li>Develop training and exercise programs that target gaps in emergency management capabilities.
</li><li> 
Identify opportunities to integrate more private and public sector stakeholders into training and exercises.
</li><li> 
Use regional governance structures to develop and promote training and exercise opportunities.
</li><li> Leverage technologies, conferences and workshops to increase training and exercise opportunities.
</li><li> Promote awareness and cross-training among local and national personnel.
</li><li> Identify and share best practices on processes and recognize trained communications personnel.
</li><li> Ensure that the capacity building process is continuous and accounts for the turnover in personnel.
</li>
</ul><p>
The last topic concerns support for "People with Specific Needs".
</p><p>
Early warning systems can be challenging for people with specific needs such as persons with disabilities, children, the elderly, migrant workers, the unemployed, people with lack of connectivity skills and those displaced from homes due to previous disasters.
</p><p>
Here are recommendations for more inclusive Early Warning Systems:
</p>
<ul>
<li>Consult with members of vulnerable populations directly and facilitate their involvement at all stages of the early warning process.
</li><li> Use multiple strategies and mechanisms to promote accessible early warnings, including legislation, policy, regulation, and codes of conduct.
</li><li> Build the capacity of vulnerable populations to receive early warnings, through programs to raise awareness, trainings and skills development
</li><li> Conduct public awareness campaigns in multiple accessible formats in different languages.
</li><li> Be aware of the potential for misuse of personal data of vulnerable populations in disaster situations, and develop ethical norms and standards for data sharing.
</li><li> Review early warning efforts to assess any challenges for vulnerable groups, discuss lessons learned, and undertake efforts to fix any issues.
</li>
</ul>	  
<p>
Before concluding, let's review the Major Topics.
</p>
<p>
<strong>Types of Disaster by Hazard Threat</strong>
</p><p>
Different kinds of disasters result from one or more different kinds of hazard events. The vulnerability of a country depends on its particular hazard threats.
Any broadcaster should know what kinds of hazard threats are likely in the particular places where the audience is located.
</p><p>
<strong>Phases of Disaster Management</strong>
</p><p>
UNDRR recognizes a cycle with four phases: Mitigation, Preparedness, Response, and Recovery.
</p><p>
<strong>Many Roles Pulling Together</strong>
</p><p>
Organizations that might play a crucial role in a major emergency include: Governments and International Institutions, NGOs, Humanitarian Aid Agencies, and Volunteer Organizations. Broadcasters and other media play a crucial role in disseminating relevant information. Coordination among the many stakeholders involved in a disaster is always a challenge.
</p><p>
<strong>Laws, Regulations, Policies, and Plans</strong>
</p><p>
National legislation, rules, and regulations are very important to emergency management.  Broadcasters must be aware also of national disaster risk management plans. The major emergency management actors in the country should have developed Standard Operating Procedures (SOPs). Of course, in actual practice we should anticipate that not all actors will adhere completely to the published plans and procedures  in every emergency.
</p><p>
<strong>Early Warning Systems in Context</strong>
</p><p>
Target G of the DRR Sendai Framework calls for nations to substantially increase the availability of and access to multi-hazard early warning systems.
</p><p>
Early warning systems seek to provide timely and actionable information, to the public as well as to others involved in the emergency. Yet, research shows that people in harm's way can find emergency alert messages confusing or not compelling. Alerting authorities as well as broadcasters need to focus on reaching the audience in terms they understand. And, we saw that early warning systems reach people in harm's way through an increasingly diverse array of channels as new technologies arise.
</p><p>
The international standard Common Alerting Protocol (CAP) is used for all manner of hazard threats. CAP helps to clearly communicate the Key Facts in emergency messages, and CAP can be used  on almost any media.
</p><p>
<strong>Capacity Building, Training, and Drills are very important for Emergency management in general and for Early Warning specifically.</strong>
</p><p>
Such efforts should: Target gaps; Leverage technologies and workshops;  Share best practices; and, Ensure continuous training.
</p><p>
<strong>People with Specific Needs</strong>
</p><p>
To address people with specific needs, those involved in Early Warning Systems should: Consult with members of vulnerable populations directly;
</p><p>
Build the capacity of vulnerable populations to receive early warnings;
</p><p>
Conduct public awareness campaigns; Be mindful of protection for personal data and develop ethical norms and standards for data sharing.  
</p> 	  
	  
