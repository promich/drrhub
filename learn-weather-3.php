	  <p>

About 85 tropical storms with maximum sustained wind speed near centre equal or over 63km/h form annually over the warm tropical ocean of the globe. Among them, 45 reach the intensity with surface winds speed above 118 km/h, as tropical cyclone/hurricane/typhoon - depending on the basin. Out of the 85 tropical storms, 72% form in the northern hemisphere, and 28% in the southern hemisphere. Different terminology is used for this weather phenomenon depending on the location:
	  </p>
	  <p>

In the Caribbean Sea, the Gulf of Mexico, the North Atlantic Ocean and the eastern and central North Pacific Ocean, it is called “hurricane.”
	  </p>
	  <p>
In the western North Pacific, it is called “typhoon.”
	  </p>
	  <p>
In the Bay of Bengal and Arabian Sea, it is called “cyclone.”
	  </p>
	  <p>
In western South Pacific and southeast Indian Ocean, it is called “severe tropical cyclone.”
	  </p>
	  <p>
In the southwest Indian Ocean, it is called “tropical cyclone.”
	  </p>
	  <p>
No matter whether they are called tropical cyclones, hurricanes or typhoons, they are among the most devastating of all natural hazards. Their potential for wrecking havoc caused by the violent winds, torrential rainfall and associated storm surges, floods, tornadoes and landslide or mudslides is enhanced by the extent of the areas they affect, their severity, the frequency of occurrence and the vulnerability of the impacted areas. Every year several tropical cyclones cause sudden-onset disasters of varying harshness, causing loss of life, human suffering, destruction of property, severe disruption of normal activities and set-back to social and economic advances. Tropical cyclones are monitored globally and forecasted daily through the WMO Global Tropical Cyclone Warning System. The WMO’s global operational network enables observation, data exchange, regional forecasting and analysis for tropical cyclones.
	  </p>
	  <p>
Tropical Cyclones Warning Centres
	  </p>
	  <p>
		  The Tropical Cyclone - Regional Specialized Meteorological Centres are located in Honolulu, La Réunion, Miami, Nadi, New Delhi and Tokyo. The Tropical Cyclone Warning Centres are located in Brisbane, Darwin, Perth, Wellington, Port Moresby and Jakarta.
	  </p>
	  <p>

The six specialized Tropical Cyclone Warning Centres together with the six regional centres have regional responsibility to provide advisories and bulletins with real-time basic meteorological information on all tropical cyclones, hurricanes, typhoons everywhere in the world. Tropical cyclones above a certain strength are given names in the interests of public safety.
	  </p>
	  <p>

Tropical Cyclone Warning Centres also provide reliable information from a clearly defined source on the tropical cyclone's location and size and its present and forecast movement and intensity. Although their functions are similar to their large regional centres, the specialized tropical cyclone warning centres are also responsible for providing local warnings to meet their national responsibilities. The official National Warnings often form part of the regional advisory or summary. 
	  </p>
	  <p>

At the national level, National Meteorological Services issue tropical cyclone warnings to designated government authorities, media, and public. Note only governments can put warnings into effect to trigger responses from civil protective agencies and disaster risk managers.
	  </p>
	  <p>
In addition to the day-to-day operations, the WMO Regional Tropical Cyclone Centres carry out training on an ongoing basis. Enhancing forecasting skill of the tropical meteorologists and the tropical cyclone models. The regional Tropical Cyclone centres also ensure forecasts are integrated into disaster risk management and response planning.
	  </p>
	  <p>

These centres provide critical information on tropical cyclones needed for preventive, preparedness and response measures that can reduce the impacts of potential disasters caused by tropical cyclones.
	  </p>
	  <p>


Forecast uncertainty with Tropical Cyclones.
	  </p>
	  <p>
Uncertainty exists when you attempt to track Tropical Cyclones. Tropical cyclone forecasting has come a long way since the first hurricane was detected officially in 1851.
	  </p>
	  <p>
Only after 1954 did forecasters issue the first recorded 24 h forecasts of Atlantic tropical storms and hurricanes.
	  </p>
	  <p>
The WMO regional centre Miami National Hurricane Center issues an official forecast every 6 hours of the centre position, maximum sustained winds, wind speed, and radius of each of the four quadrants (northeast, southeast, southwest, northwest)
surrounding the cyclone.
	  </p>
	  <p>
The National Hurricane Centre's errors when it comes to predicting the storm track and intensity have significantly been reduced over the last decade. In fact, 48-hour forecasts are as accurate today as 24-hour forecasts were 10 years ago. Many of those improvements are due to long-term research efforts.


	  </p>
