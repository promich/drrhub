<p> 
Hopefully by now you should have a pretty good idea of the basics of DRR.
</p><p>
Let's try to apply that knowledge and information to news and current affairs.
</p><p>
Some of the bigger broadcasters  have established units and specialist correspondents to deal with DRR, Climate issues and environmental stories and most of you don't have the resources to do that.
</p><p>
But what we'd like to prove to you is that DRR extends into all kinds of journalistic disciplines and cuts across beats. I’d like to give you 10 good reasons to report on DRR.
</p><p>
Firstly it's a political issue.
</p><p>
As disasters continue to rise and people demand more action from their governments to take preventative action, DRR is a  growing topic that's forcing politicians from across the spectrum to act - it's a hot potato that's going to get hotter!.
</p><p>
The effects of climate change have already led to civil unrest, protests and even armed insurgencies in some countries.
</p><p>
The increasing damage from disasters will also make the case for stronger governance, closer regional cooperation and international collaboration.
</p><p>
Two. Natural hazards are increasing - they're on the rise.
</p><p>
Climate change, poverty, urban risks and environmental degradation are exposing more people to more problems and an entirely new scale of damage and devastation.
</p><p>
Three. DRR is of course, an economic issue.
</p><p>
Disasters cause damage worth 130 billion US Dollars every year, on average. It's the on-going effects of a disaster that's hard to quantify
</p><p>
But it’s not just economic losses. Disasters devastate livelihoods - sometimes for years.It's the on-going effects of a disaster that are hard to quantify. If a storm rips up a palm tree - it'll take between 4 and 6 years to grow back to it's full height.
</p><p>
Four. DRR is a human rights issue.
</p><p>
It’s the weakest and poorest who suffer most in disasters, raising fundamental human rights challenges.
</p><p>
They often lead to a "blame -game" where fingers are pointed at certain groups, they're made into scapegoats and are sometimes demonised -  this again can be fuelled by political agendas and commercial interests.
</p><p>
Five. DRR is an environmental issue.
</p><p>
Most disasters are the spark for environmental stories - what were the causes and what were the effects on the world around us.... and as a result what could have been done to avoid the effects or at least to mitigate the damage.... questions after the event are  asked about  the preventive steps that could have been taken.
</p><p>
The cultural angles are always a good source of stories.
</p><p>
People have different perceptions of hazards and they react in different ways. Some see them as acts of God -  Others draw on traditional knowledge and practices for resilience. This is an area ripe for storytelling where cross- generational links can be examined - the coming together of experience and wisdom with more modern technologies and applications.
</p><p>
 DRR is a gender issue.
</p><p>
There are many reasons why women across the world, are more vulnerable to disasters.
</p><p>
They may have a more subordinate position in the family. They may lack education, income, mobility and nutrition, relative to the men within their societies. Stories of women’s empowerment are always interesting and are key to a more equal society.
</p><p>
Eight. DRR is not only a disaster story.
</p><p>
That is, DRR stories don’t just have to be about the disasters themselves. You could be covering current risks and dangers - yes - , but you can also commemorate past disasters, anniversaries and lesons learnt (or not!); You could report on disaster recovery projects and reconstruction efforts- these can be positive stories that show an audience that they're not alone and that efforts are being made to avoid future disasters… All good angles that show how a broadcaster is working on behalf of their audience.
</p><p>
Nine. DRR is a health issue.
</p><p>
As Covid-19 has shown, the resilience of health systems is essential to strengthen communities’ resilience against natural and also biological hazards.
</p><p>
 Finally on this list - DRR is often an investigative story.
</p><p>
Journalists do more than just break the news. DRR is an area that cries out for digging under the surface, probing, scrutinising - and holding authorities and organisations to account. It's questioning on behalf of those who watch and listen to us.
</p><p>
Now that’s just 10 reasons. There are more of course. And this is where your skills and responsibilities as broadcasters come into play. Go into the Media Hub for more details and  more information and hopefully you'll join me or one of my colleagues in a live session that will put more meat on the skeleton and we'll be able to interact and get you involved on a more practical level.</p>
 
