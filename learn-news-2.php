<p> 
There's always a lot of theory offered on courses but we also want to address the practical issues that face broadcasters in taking on new challenges. So this short section is called "TIPS"... a guide based on experience of what needs to be put in place from an editor's perspective and for reporters.
</p><p>
The first major question is "How do we ensure that DRR issues get good coverage?" If only there was a magic wand!
</p><p>
What we have to do initially is to set out a template for editors - the people who shape the content of programmes and programming.
</p><p>
They need a policy about WHO covers disasters. Just like you need someone you can turn to for specialist political knowledge or economic insight, you need at least one person who can deal with DRR, Climate, the Environment... and get them trained - invest some time in getting reporters up to speed.
Have an internal plan to cope with disasters....who does what, and what is the back up?
</p><p>
Have a plan to alert and issue early warning messages. That's the least that an audience would expect from a responsible broadcaster.
</p><p>
Ok..a disaster happens and at least you have a plan to put into action before you're consumed by the events that can sometimes turn into chaos. There are more checklists on the media hub dealing with the live/ occurring disaster, the kind of questions that need to be asked, some practical guidelines and basically a template on how to react for all the staff involved.
</p><p>
After the fury of the event itself......The next step for the editor is to allow time for the post-event questions to be asked. Causes have to be investigated, lessons have to be learnt.... build that into your timeframe.
</p><p>
It's also very important to meet the people who are involved as stakeholders before disasters occur. That is, organise meetings with National Disaster Management people, NGO's / Red Cross, and make sure that vulnerable groups are included.... it's the editor's responsibility to sensitise and initiate education in this sense.
</p><p>
For reporters and researchers,  build up your contacts, - your editors might have opened the door - you have to step in. Line up your experts, know their specialities. Have a list for early warning systems, urban risks, climate change, gender, environmental, national and local meteorological departments, disaster managers, civil protection and leading NGOs.
</p><p>
 Basically cover all the possible disasters likely to happen in your area by having an expert to guide you.
 </p><p>
Keep records,  and statistics on past events - short, sharp facts have an impact.
</p><p>
Know the areas that are prone to disasters and keep a track record of lessons learnt - or not!
</p><p>
Know what the authorities have done or promised in the past and what factors can make a disaster worse.
</p><p>
Base your information on the facts and sound scientific knowledge.
</p><p>
And always listen to the people involved-  help safeguard communities - lives and livelihoods are paramount.
</p><p>
This is not a basic journalism course but remember the key elements to a good broadcast story that are more important than ever in trying to reduce risks...
Be clear - speak to your audience.... avoid jargon!
</p><p>
Let your pictures lead and tell your story concisely- audiences usually only get one chance to understand.
</p><p>
Try to choose good interviewees... personalise the story because "people like people".....
</p><p>
Most of your story will probably involve emotion from interviewees... so make sure your facts in commentary are correct.
</p><p>
It's not always possible under difficult circumstances but a story with a beginning, a middle and an end always resonates with an audience.</p>
</p>
