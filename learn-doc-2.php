	  <p>
No matter what kind of programming you're involved in, if your editor or producer has a blank sheet for a particular day or episode, the people who come up with good ideas are the ones who are appreciated more than others. And there's always a fall back that can generate programme ideas. Its called the CALENDAR ! Going back in history and making it relevant to today is a treasure trove for broadcasters. Think of that in terms of DRR. Did that storm really happen 20 years ago ? I wonder how the village that was flooded last year has managed ?  It'll be the anniversary of Hurricane Irma in a months's time - how have the re-building plans come on? So many programmes can be devised based on an historic event and brought into a modern day discussion or debate... risk reduction issues being updated.</p><p>

Another good use of the Calendar is "the seasons". Some ecologists use a 6 season model for temperate climates, others stick to a traditional 4 season year and of course in tropical regions only two main seasons are considered. But whether it's 2 or 4 or 6, valuable programming can be built around the changes and no one year will be the same as another. So devise content that will guide your viewers through the expected or possible changes with advice how to be more resilient in extreme cases and give examples how other communities or countries have adapted or are adapting. You'll find willing guidance and assistance from so many organisations - the World Meteorological Organisation, the Food and Agricultural Organisation and of course a whole host of NGO's including the Red Cross/ Red Crescent.</p><p>


The other rich source of material is Summits and Conferences. The mistake that many broadcasters make is to try to cover conferences with a report of what was said rather than try to illustrate the issues in story form. Let me give you an example.  One intergovernmental organisation that's providing a significant contribution to the protection, the sustainability and the development of their region is SPREP - the Secretariat of the Pacific Regional Environment Programme based in Apia, Samoa.
</p><p>
There are literally hundreds of various courses, workshops and conferences that occur annually across the Asia- Pacific region alone. Whereas the variety and  quantity of activities are to be commended, the outcomes are not as effective as they should be - and one of the reasons for that is because broadcasters mainly, don't take advantage of what they're offering. During early 2019 for example, Fiji was the venue for a major Pacific Resilience Event at the University of The South Pacific in Suva  and at the same time, the ABU were conducting an intensive workshop for broadcasters from a number of Pacific Islands with the express objective of improving climate resilience. With the permission of SPREP ,they targeted the conference with an aim of producing stories for their local output - not by recording the conference itself but by taking an angle on a story and turning it into a broadcast item. More than twenty five broadcast items were produced from the 3 day conference and broadcast across the region, and entire pilot programmes -“Tales of Resilience”-  were broadcast on FBC Fiji .
A little bit of televisual thinking, a bit of imagination and the result was an interesting broadcast output.
</p> 	  
	  
