<p>
Let's review the major topics.
</p><p>
Opportunities abound for broadcasters to improve public alerting by leveraging CAP. CAP can address the challenge of our crazy patchwork of alerting systems, today--across communities, nations, and internationally. The all-media, all-hazards approach embraced by the CAP standard enhances the efficiency and effectiveness of public alerting.
</p><p>
CAP messages contain not only text values for human readers, but coded values useful for automated processing. We looked at a "raw" CAP message in its machine-friendly XML format, as well as its human-friendly format as seen on a Web browser.
</p><p>
CAP can help assure that alerts are timely and that alerts reach everyone who needs them, and only those who need them.
</p><p>
CAP allows an alerting authority to activate multiple alerting systems with a single input. With CAP-enabled systems, alerts from many sources can be compiled for situational awareness so that emergency managers can fill out their "Common Operating Picture".
</p><p>
Organizations that are authorized to perform the function of alerting should be registered in the international Register of Alerting Authorities.
</p><p>
We saw a sample of CAP-enabled alerting systems around the world.
</p><p>
And our last topic concerned CAP Alert Hubs.
