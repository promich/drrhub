<p> 
Let's move on a little from the average news programme...  there's what I call "a jigsaw" of factual programmes that we need to deal with and we need to make sense of what content should fit where. About 60% of all TV programmes are factual - news, magazine, consumer, chat shows, current affairs and documentaries. All of them inform and try to  educate the viewer through a variety of points of view.
</p><p>
So if we think of the news as the "headline`" provider... it gives the viewer an instant snapshot of what's just happened or what's actually happening. It can create empathy, it can spark the audience to want to help.  It can encourage social/political change by bringing the issues to the public arena but that usually means that an audience would like to know more than just the bare facts. 
</p><p>
 News reports are short and to the point and they try to tell a story that viewers will understand quickly and easily - usually in one or two minutes. The viewer is being hit by a fact or an image or a sound peak for a short period of time.
 </p><p>
A Feature item is longer in length, perhaps up to 4 or even 5 minutes that gives the reporter or producer more time to develop the story. Take a look at this example from a training course held in Samoa for Pacific Broadcasters commemorating an anniversary of a disaster.
</p><p>
It more than scratches the surface and shows the potential of reminding people how aware and how resilient they need to be. Editors should perhaps look to incorporate such features into their programming.
</p><p>
A Current Affairs programme is the next progression if you like, to look at an issue or a story in real depth. It often takes the form of a thirty minute programme which will investigate a story and give detail and explore arguments.DRR issues are prime material for these programmes.
</p><p>
All of the values that we've mentioned, - investigative, asking on behalf of an audience, holding authorities and organisations to account, building  an awareness that the risks involved in disasters can be reduced. They obviously take time to research and produce but the trust that they can build between a broadcaster and an audience is immense. If safeguarding lives and livelihoods is the result of such programmes, we, are being proactive in our role and responsibilities.</p>
 
