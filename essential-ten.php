                          <h4 class="h3 mb-3">Philippine climate advocates seek candidates who will boost climate and disaster resiliency</h4>
                          <p>Recognized as one of the most disaster-prone countries in the world, the Philippines needs to elect leaders and policymakers who can face up to the adversities brought about by climate change and disasters, various climate advocates said on Tuesday.
</p>

<p>In a statement, climate group Disaster Risk Reduction Network Philippines (DRRNetPhils) is urging voters in the coming May 2016 national and local elections to select candidates who will boost climate and disaster resilience in the country.</p>
<p>The group particularly noted that the next president should include Disaster Risk Reduction and Climate Change Adaptation (DRR-CCA) in his or her platform and make it a top development agenda. 'The public demands a president with a vision of resiliency, a concrete plan to get there, and greater accountability for the thousands who have suffered from the effects of disaster and climate change,' said DRRNetPhils.</p>
<p>Although a strong executive arm is needed when it comes to DRR-CCA, the climate group also said that lawmakers are needed to reduce vulnerabilities of Filipinos to disaster and climate change while enhancing capacities and resiliency through policy reform and decisive action.</p>
<p>'As much as we need strong law enforcers, we need just lawmakers. We need lawmakers who can craft and support policies that address the all-too-often neglected rights of elderly people, women, children, persons with disabilities, indigenous peoples, urban poor, fisher folk, agricultural laborers, and factory workers before, during, and after disasters because they are the ones disproportionately affected and have the least capacity to recover from disasters,' DRRNetPhils stressed.' This is because laws are and remain to be our first line of defense, especially by the marginalized and vulnerable,' it said. Currently, the Philippines has two policies on disaster management and climate change adaptation -- the Climate Change Act of 2009 (Republic Act No. 9729) and the Philippine Disaster Risk Reduction and Management Act of 2010 (R.A. 10174).</p>
<p>R.A. 9729 aims to mainstream climate change adaptation into government policy and establish a framework strategy. This law created the Climate Change Commission.</p> 
<p>Government agencies and local government units (LGUs) should allocate from their annual appropriations enough funds for formulation, development, and implementation of projects under this law. Meanwhile, R.A. 10121, which focuses on prevention and mitigation, is an act mandated to strengthen disaster management in the Philippines. It replaced the National Disaster Coordinating Council (NDCC) with the National Disaster Risk Reduction and Management Council (NDRRMC) as the focal body. Headed by the Office of Civil Defense as its implementing agency, NDRRMC coordinates with the regional, provincial and local DRRM councils.</p>
<p>The law also mandated LGUs to allot 5.0 percent of their yearly budgets to disaster mitigation, preparation, and response. Of the local disaster fund, 70 percent shall be allocated for pre-disaster measures while the rest shall be used as Quick Response Fund which serves as a stand-by fund for relief and recovery programs.</p>
</p>

