<h5>Disaster and gender statistics</h5>
<p>Neumayer and Plmper analyzed disasters in 141 countries and found that, when it came to deaths, gender differences were directly linked to women’s economic and social rights; in societies where women and men enjoyed equal rights, disasters caused the same number of deaths in both sexes.</p>
<p>They also confirmed that discrepancies were the result of existing inequalities. For example, boys were given preferential treatment during rescue efforts and, following disasters, both women and girls suffered more from shortages of food and economic resources (Neumayer and Plmper, 2007). </p>
