                          <h4 class="h3 mb-3">Time for mining to deliver on zero casualty policy</h4>
                          <h5 class="h5 mb-3">Op-Ed | 15 February 2019 | By Mami Mizutori</h4>
                          <p>The decision by Vale, the world’s largest manufacturer of iron ore, to decommission ten of its tailings dams in Brazil is welcome, but it will seem too little, and too late, for those who have lost loved ones and seen their environments destroyed by the failures of such structures.</p>
                          
<p>Tailings dam failures over the last five years have made the headlines in Canada, Mexico, Brazil, China, the USA and Israel. </p>

<p>Not all have resulted in loss of life, but the damage to the environment has been extensive and raises enormous questions and safety concerns over what conditions are like at the world’s 30,000 industrial mines.</p>

<p>In 2001, the International Commission on Large Dams issued its report “Tailings Dams: Risk of Dangerous Occurrences”, which examined 221 failures. It found all of them could have been avoided.
The technical knowledge exists to build and maintain safe facilities. As a recent UN Environment Programme assessment put it: “an inadequate commitment to safe storage combined with poor management was the cause of most failures.”</p>

<p>It remains to be seen if the investigation now underway by Vale and the Brazilian authorities discovers any other factors in the failure in critical infrastructure at the Córrego de Feijão mine in Brumadinho, Brazil, which has likely claimed hundreds of lives.</p>

<p>Since the 2001 landmark report, one other risk factor for the mining industry has come into sharp relief and that is climate change. The increasing variability in global weather systems, including rainfall patterns, floods and storms, must be factored into disaster risk management at any mining site.</p>

<p>Another fundamental concern is to ensure that disaster risk governance is at the forefront of the mining industry’s approach. The safety and well-being of their employees and of the people living in surrounding communities must be paramount. It must not be profit at all costs – or indeed at any cost. Risk-informed decision making needs to be the norm.</p>

<p>The Brumadinho disaster follows the 2015 Mariana disaster at a facility co-owned by Vale and BHP Billiton which was ranked as Brazil’s worst environmental disaster. Nineteen people died as a tidal mass of tailings slurry was released, destroying the village of Bento Rodrigues; traveling 620 kms to the Atlantic Ocean and laying waste to entire fish populations, 1,400 hectares of forest and 663 kms of water works.</p>

<p>That kind of forensic detail is not yet available on the damage done by last weekend’s disaster but the long-term consequences for the habitat and the affected communities are likely to be great.
Vale CEO, Fabio Schvartsman, has said that the company has created a working group to raise the safety standard of the company’s dams.</p>

<p>This will not satisfy public opinion. Inadequate risk governance is a key driver and enabler of disasters and until there is proper and adequate regulatory oversight of these facilities, public confidence in their safety will not be restored.</p>

<p>It is time that the recommendations made in 2001 were implemented. Further recommendations from the UN Environment Programme should also be taken into account, including a safety first approach regardless of cost.</p>

<p>The global plan for reducing disaster losses, the Sendai Framework for Disaster Risk Reduction, seeks to not only establish concrete ways in which critical infrastructure can be risk-proofed, but also exert influence with both governments and the private sector to banish complacency around the technological and environmental threats posed by such facilities.</p>

<p>I would urge the thousands of communities living with this potential threat to ensure their voices are heard and concerns are captured in both national and local strategies for reducing disaster risk.
One way the mining industry could facilitate this would be by helping to establish an open database of mine sites and tailings storage facilities.</p>

<p>This would be a fitting and lasting way for all concerned to remember those who have lost their lives in such disasters and to demonstrate that the mining industry is not just paying lip service to a zero-casualty policy.</p>
</p>

