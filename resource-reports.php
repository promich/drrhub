<div class="col-md-9 col-lg-5 mt-sm-2 mt-md-7">
	<p class="lead mb-4">The UN Global Assessment Report on Disaster Risk Reduction (GAR) is the flagship report of the United Nations on worldwide efforts to reduce disaster risk. The GAR is published every two years since 2009 by the UN Office for Disaster Risk Reduction (UNDRR) and is distilled from public and private disaster risk-related science and research. It is a global assessment of disaster risk and a comprehensive review and analysis of the natural hazards affecting humanity, disaster risk and risk management.</p>


<h4 class="">Global Assessment Report 2019</h4>
<p class="mb-4">GAR2019 outlines major risks to human life and material property, ranging from air pollution and biological hazards, through to earthquakes, drought, and climate change. It warns that unsustainable patterns of economic activity hide the build-up of systemic risks across sectors, and that there is growing potential for one disaster to produce or exacerbate another, as happens often in the case of heavy rains which trigger landslides and mudslides following wildfires or periods of long drought. The era of hazard-by-hazard risk reduction is over, it says: present and future approaches to managing risk require an understanding of the systemic nature of risk.</p>

<h4 class="">Global Assessment Report 2017 / GAR Atlas: Unveiling Global Disaster Risk</h4>
<p class="mb-4">The GAR Atlas presents the risk associated with a number of hazards (earthquakes, tsunamis, riverine flooding, cyclonic winds and storm surge), bringing national data together into a global picture. The GAR Atlas provides globally comparable multi-hazard risk metrics and enables comparison of risk levels between countries and regions and across hazard types. In this way, it facilitates a better understanding of the global risk landscape, enabling the estimation of the order of magnitude of probable losses in each country while taking into account the risk contributions from different hazards.</p>

<h4 class="">Global Assessment Report 2015 / Making Development Sustainable: The Future of Disaster Risk Management</h4>
<p class="mb-4">The 2015 edition presents the case for a broad reinterpretation of disaster risk reduction. It questions whether the way in which disaster risk reduction was approached in the past is really fit for purpose in a world now threatened by catastrophic increases in disaster risk. It shows why the focus of disaster risk reduction needs to move from managing disasters to managing risks if it is to contribute to making development sustainable.</p>

<h4 class="">Global Assessment Report 2013 / From Shared Risk to Shared Value: The Business Case for Disaster Risk Reduction</h4>
<p class="mb-4">The 2013 edition explored the nexus between private investment and disaster risk and showed how businesses can invest in managing their disaster risks to reduce the costs and interruptions represented by  disaster losses and impacts, and how they can enhance performance and reputation by minimizing uncertainty and unpredictability.</p>

<h4 class="">Global Assessment Report 2011 / Revealing Risk, Redefining Development </h4>
<p class="mb-4">The 2011 edition identified effective public policies to address the disaster risk–poverty nexus and the political and economic imperatives and constraints for increased public investment in disaster risk reduction. GAR2011 produced risk profiles for a number of countries in order to demonstrate how a risk-layered approach to managing disaster risks could maximize benefits while reducing costs. It also provides guidance and suggestions to governments and non-governmental actors alike, on how they can, together, reduce disaster risks.</p>

<h4 class="">Global Assessment Report 2009 / Risk and poverty in a changing climate</h4>
<p class="mb-4">The 2009 edition of the GAR provided evidence that disaster risk is disproportionately concentrated in lower-income countries with weak governance and explores how underlying drivers such as badly planned and managed urban development, vulnerable rural livelihoods, environmental degradation, poverty and inequality, further generate and accumulate disaster risk in low-income communities and households.
</p>


